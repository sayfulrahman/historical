                <p><strong>
                        <font face="Arial">Carla' tells the steps to take to get information
                            from the military:</font>
                    </strong></p>
                <p>
                    <font face="Arial"><strong>FIRST STEPS I TOOK:</strong> The first record you
                        should get is the record from the Department of Veterans Affairs (VA).&nbsp;
                        This record will have the veteran's service number in it and also may have a
                        social security number.&nbsp; It will also show designated next of kin and will
                        contain marriage records and birth certificates.&nbsp; To get these records call
                        (800) 827-1000.&nbsp; This will ring at your regional office.&nbsp; They will be
                        able to tell you where your veteran's record is and where to write.&nbsp; Then
                        write to that address requesting a complete copy of everything in their
                        file.&nbsp; ( Remember you are only eligible for these records if you are next
                        of kin and the veteran was killed or missing in action.) If you already have the
                        selective service number, you will want to write to the National Personnel
                        Records Center for the veteran's &quot;jacket&quot; right away also but do this
                        only after you have the selective service number.&nbsp; Ask for a copy of
                        everything they have in your veteran's personnel records,
                        &quot;jackets.&quot;&nbsp; Also ask for the &quot;MED&quot; ( <strong>medical</strong>
                        ) records.&nbsp; (You must have a selective service number and ask for
                        &quot;MED&quot; (<strong> medical</strong> )records specifically as they are
                        only filed under the selective service number.&nbsp; These are not file by
                        name).&nbsp; Additionally, you can apply for any medals which you lost or never
                        received.&nbsp;&nbsp; Write to: National Personnel Records Center, Military
                        Personnel Records, 9700 Page Boulevard St. Louis MO 63132</font>
                </p>
