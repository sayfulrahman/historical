                <h2 align="center">
                    <font face="Arial"><strong>In Memory of My Dad</strong></font>
                </h2>
                <p>
                    <font face="Arial">This is an open letter to all of those of you who feel
                        like I do that I never got to say good bye or are searching for that answer that
                        has haunted us for 50 years.&nbsp; Some of us are looking for a person or a
                        place, some are looking for just someone to talk to about their thoughts.&nbsp;
                        Or need help in deciding what we are looking for?&nbsp; Most of us are looking
                        for the peace, for the deep wounds we have from W.W.II perhaps another war. I
                        personally lost my dad to the war on November 27, 1944 at the age of (barely)
                        five.&nbsp; He was only 28 himself, and I understand maybe one of the older
                        soldiers.&nbsp; He wanted to go and help our country stay free as so many
                        did.&nbsp; If only we could have some answers from, where did they go, or what
                        can I learn about them.&nbsp; I have memories and family who told me about my
                        dad.&nbsp; I'd still like to know what he was like through the eyes of people
                        who were with him in the last months or moments of his short life.&nbsp; I feel
                        so much pride in my dad and what he gave and we gave for our country, but still
                        the little kid never forgets the love of a daddy they didn't get to grow up
                        with.</font>
                </p>
                <p>
                    <font face="Arial">My mother and I had a home and she went to work to support
                        us, as many of you know there wasn't much pension. So many women either worked
                        or re married, or even had to give up their children.&nbsp; Some of those
                        stories are varied, my mother did go to work and then remarried.&nbsp; In that
                        time people expected you to go on without really looking at your feelings.&nbsp;
                        I know that a lot of the veterans came back to broken homes and bad memories of
                        the war.&nbsp; Some of course were real lucky and had their families waiting and
                        went on even with the bad memories.&nbsp; The ones who came home we all are so
                        thankful for and some are now opening up to share with us, I know how hard this
                        must be, because they lost their buddies, that at one moment they were fighting
                        with and then he was gone. My dad was one who was buried in Holland instead of
                        being brought home. There is a lot of pain attached to this because he didn't
                        come home for me or our family to be able to say goodbye too.&nbsp; Or to have a
                        answer to where he was to a little kid.&nbsp; I always believed my daddy would
                        come home and knew someone was keeping him from me.&nbsp; Even though I was told
                        and somewhere I might have understood there was a burial place but I couldn't
                        see it.&nbsp; So when I was 23 years old I finally went to Margraten, Holland
                        where that finally resting place is.&nbsp; I saw a beautiful place with
                        thousands of white crosses.&nbsp; I wanted to stay close to that place and feel
                        my daddy but knew I couldn't.&nbsp; It did help me, but not to the place that
                        I've finally started to reach now.&nbsp; One of the veteran's I've spoken to
                        said &quot;he's with his buddies&quot;.&nbsp; That for some reason has made a
                        difference to me and given me a different way to look at it.&nbsp; I as a
                        Christian so I understand that is not where my dad is, but as a child you don't
                        understand and when you can't see that place its not real. I still can't just go
                        by and place the flowers or talk to him but I do like the fact he is with his
                        buddies.&nbsp; Talking to these veteran's is such a gift it is hard to put into
                        words. I found an organization that has helped me find a lot of peace and
                        inspiration to go on with my life finally.&nbsp; I'm letting myself open up and
                        responding to help and things are coming to my attention that probably were
                        always there.&nbsp; Just didn't see them, wouldn't allow my self to see because
                        of the pain.&nbsp; So I've probably compounded my pain over the years, now
                        finding people like these who have helped with this page on this web site to
                        help me heal.&nbsp; Also learn to feel self worth that a daughter grows up
                        without when loosing her daddy so young.&nbsp;</font>
                </p>
                <p>
                    <font face="Arial">I'm thankful for the help I've received and that's the
                        reason for this page is to offer you support in you search. Or a place to tell
                        you tell something you need to share. If we don't know the answers we can search
                        together I'm learning by listening and asking.&nbsp; That's been a tough thing
                        for me to learn to ask.&nbsp; Some of the hardest things to learn are there are
                        really people out here who care.&nbsp; These are some of the wounds, from a war
                        that I didn't fight, but I lost one of the very most important people to me in
                        my life in.. I owe this opportunity and this page to Howard Swonger who served
                        with the 82nd Reconnaissance Battalion, 2nd Armored Division.&nbsp; He's been a
                        very supporting and generous friend with his time and efforts to helping me and
                        anyone who comes to this site.&nbsp; He and his son have done such a great job
                        of putting this site as well as the &quot;Hell On Wheels&quot; site dedicated to
                        the 2nd Armored together.&nbsp; That's how I found Howard searching on that
                        site. I'm here to somehow to offer my hand to someone who has felt like I have.
                        This is the reason for this letter and page. &quot;GOD BLESS AMERICA&quot; and
                        the memory of those would served. Carla Sue. Proud daughter of Pvt. Morris
                        Harold &quot;Buster&quot; Kingery,&nbsp; 41st Infantry Regiment, <a
                            href="http://www.2ndarmoredhellonwheels.com/">2nd
                            Armored Division, &quot; Hell on Wheels </a>Co. G, 3rd Battalion.</font>
                </p>
