    <h4><a name="medals">
            <font face="Verdana">Medals</font>
        </a></h4>
    <p>
        <font face="Verdana">Medals awarded while in active service will be issued by
            the appropriate service if requested by veterans or, if deceased, their next of
            kin. Requests for medals from the Navy, Marine Corps and Coast Guard should be
            sent to the U.S. Navy Liaison Office, National Personnel Records Center, Room
            3475, 9700 Page Blvd., St. Louis, MO 63132-5100. Requests for medals from the
            Army should be sent to Army Commander, U.S. Army Reserve Personnel Center, ATTN:
            DARP-PAS-EAW, 9700 Page Blvd., St. Louis, MO 63132-5100. Requests for medals
            from the Air Force should be sent to the National Personnel Records Center
            (Military Personnel Records), 9700 Page Blvd., St. Louis, MO 63132-5100.</font>
    </p>
    <p>
        <font face="Verdana">The veteran's full name should be printed or typed, so
            that it can be read clearly. The request must contain the signature of the
            veteran or the signature of the next of kin if the veteran is deceased. Include
            the veteran's branch of service, service number or Social Security number and
            dates of service, or at least the approximate years. If available, include a
            copy of the discharge or separation document, WDAGO Form 53-55 or DD Form 214.
            If possible, send the request on Standard Form 180, &quot;Request Pertaining To
            Military Records.&quot; These forms are generally available from VA offices or
            veterans organizations.</font>
    </p>
    <h4><a name="disch">
            <font face="Verdana">Review of Discharges</font>
        </a></h4>
    <p>
        <font face="Verdana">Each of the military services maintains a discharge review
            board with authority to change, correct, or modify discharges or dismissals that
            are not issued by a sentence of a general court martial. The board has no
            authority to address medical discharges. The veteran or - if deceased or
            incompetent - the surviving spouse, next of kin or legal representative may
            apply for a review of discharge by writing to the military department concerned,
            using Department of Defense Form 293 (DD-293). This form may be obtained at any
            VA office. If more than 15 years have passed since discharge, DD Form 149 should
            be used for applications to the Board for the Correction of Military Records.</font>
    </p>
    <p>
        <font face="Verdana">Service discharge review boards conduct hearings in
            Washington, D.C. Traveling review boards also visit selected cities to hear
            cases. In addition, the Army sends teams to locations to videotape the testimony
            of applicants for later review by a board in Washington, D.C.</font>
    </p>
    <p>
        <font face="Verdana">Discharges awarded as a result of unauthorized absence in
            excess of 180 days make persons ineligible for VA benefits regardless of action
            taken by discharge review boards, unless VA determines there were compelling
            circumstances for the absences. In addition, boards for the correction of
            military records may consider such cases. Applications to these boards are made
            with DD Form 149.</font>
    </p>
    <p>
        <font face="Verdana">Veterans with disabilities incurred or aggravated during
            active military service may qualify for medical or related benefits regardless
            of separation and characterization of service. Veterans separated
            administratively under other than honorable conditions may require that their
            discharge be reviewed for possible recharacterization, provided they file their
            appeal within 15 years of the date of separation.</font>
    </p>
    <p>
        <font face="Verdana">Questions regarding the review of a discharge may be
            addressed to the appropriate discharge review board at the following addresses:</font>
    </p>
