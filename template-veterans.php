    <h4><a name="correct">
            <font face="Verdana">Correction of Military Records</font>
        </a></h4>
    <p>
        <font face="Verdana">The secretary of a military department, acting through a
            board for correction of military records, has authority to correct any military
            record when necessary to correct an error or remove an injustice. Applications
            for correction of a military record, including review of discharges issued by
            court-martial, may be considered by a correction board. A request for correction
            generally must be filed by the veteran, survivor or legal representative within
            three years after discovery of the alleged error or injustice. The board may
            excuse failure to file within the prescribed time, however, if it finds it would
            be in the interest of justice to do so. It is the responsibility of the
            applicant to show why the filing of the application was delayed and why it would
            be in the interest of justice for the board to consider the application despite
            the delay. To justify any correction, it is necessary to show to the
            satisfaction of the board that the alleged entry or omission in the records was
            in error or unjust. Applications should include all evidence which may be
            available, such as signed statements of witnesses or a brief of arguments
            supporting the requested correction. Application must be made on DD Form 149,
            which may be obtained at any VA office. Send completed application to the
            address indicated on the form.</font>
    </p>
