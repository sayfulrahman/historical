    <h4><a name="death">
            <font face="Verdana">Death Gratuity</font>
        </a></h4>
    <p>
        <font face="Verdana">Military services provide a death gratuity of $6,000 to a
            deceased service member's spouse or children. The death gratuity is payable for
            death in active service or within 120 days thereafter from causes related to
            active service. Parents, brothers or sisters may be provided the gratuity, if
            designated by the deceased. This is paid by the last military command of the
            deceased. If the beneficiary has not been paid within a reasonable time,
            application may be made to the service concerned.</font>
    </p>
