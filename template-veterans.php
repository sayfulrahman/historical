    <h4><a name="comm">
            <font face="Verdana">Commissary and Exchange Privileges</font>
        </a></h4>
    <p>
        <font face="Verdana">The following are entitled to unlimited exchange and
            commissary store privileges in the United States: honorably discharged veterans
            with a service-connected disability rated at 100 percent, unremarried surviving
            spouses of members or retired members of the Armed Forces, recipients of the
            Medal of Honor, and certain dependents and orphans. Certain reservists and their
            dependents also are eligible. Entitlement to these privileges overseas is
            governed by international law, and privileges are available only to the extent
            agreed upon by the foreign governments concerned. Certification of total
            disability will be given by VA. Assistance in completing DD Form ll72
            (Application for Uniformed Services Identification and Privilege Card) may be
            provided by VA.</font>
