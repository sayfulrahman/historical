        <font face="Verdana">On 12 April 1045 Co. D under the command of Capt. George
            J. Karl was leading the attack on the town of Magdeburg, Germany (on the Elbe
            River) we were met with small arms fire plus one of our guys was wounded. His
            name was Pvt. Picolene (not sure spelling) He was in headquarters platoon. Upon
            entering the town I was ordered to take two men and reconnaissance this street.
            Check for any armored vehicles etc. I picked Jerry Hlavnick, who was a old-timer
            plus a experienced soldier plus Pvt. Vincent W. Paulman who was a replacement.
            Jerry (nick name WOP) took the left side of the street plus I took the right
            side. Pvt. Vincent W. Paulman took the rear, to give us covering fire if we
            needed it . About one block with out incident I saw someone move in a ally on my
            right. I ran into the alley and found a fat civilian wearing a white shirt. I
            grabbed him and slammed him against the wall and put my bayonet under his chin.
            The man was terrified. I spoke a little German and asked where the German
            soldiers were hiding. He said in the school on the corner . I pushed him in
            front of me, prodding him with my bayonet. Sure enough there was the school like
            he said. He told me that they were in the cellar. I ordered him to go down into
            the cellar and tell them to come up with their hands up or we would drop
            grenades into the cellar. He went and we waited in the school yard. Well to our
            surprise they started coming out , we could not believe how many there was. We
            counted twenty-one of them. Put. Paulman had a carbine, Jerry had a Tommy gun
            and I had M1 rifle . We lined them up and I asked Put Paulman to give them a
            search. Satisfied that they had no weapons.</font>
    </p>
    <p>
        <font face="Verdana">I was a bit of a wheeler dealer in those days and I
            thought they left all the good stuff (pistols/cameras) things that would bring a
            good price in the rear areas down in the cellar. So I asked Jerry and Paulman to
            take the prisoners back to the C.P. They asked me what I was going to do. And I
            said I was going to check the rest of the building for enemy stragglers. Well
            they left, I went down in the cellar and found four pistols, two cameras and
            some other stuff which I stashed in my combat jacket . I then proceeded to check
            out the rooms. The loot would bring one hundred bucks, a piece in the rear
            areas. In one room it had a large map. It had pins stuck in it and crayon
            markings on it. I took the map and put in my jacket and went back to the C.P..
            After stashing my loot I went to see Capt. Karl who was are C.O. well he was
            elated, the map showed all the fortifications for the area. He sent a dispatch
            rider back to battalion with the map. He said the map would save a lot of lives.
            He said &quot;Puma ill see that you get a decoration &quot;. Well its been a
            long time since this happen and I never got that decoration that Capt. Karl
            promised. But knowing what I did even saving one life was thanks enough.</font>
