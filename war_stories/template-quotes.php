        many that it would be an injustice to just name a few, and leave other's out
        that I consider to be just as good. I always wondered why nobody ever asked what
        are the names of some <strong>great privates</strong>, they were out there too,
        they were even up front fighting the enemy. (I said great, not somebody <strong>famous</strong>) When the
        enemy gets you in his sights he don't care if you are
        outstanding, or great, or famous, he shoots to kill.</font>
</p>
