</p>
<p align="left">
    <font face="Verdana"><b>Answer: </b>At that time all my friends
        were going, it was a time when most everyone was patriotic, of course there were
        some draft dodgers even then, and claims of problems to stay out of the service,
        but generally most wanted to serve, it was a different time. But there came a
        time, on our first combat mission,&nbsp; when I looked down into the valley that
        we were headed for and seen all the shooting down there,&nbsp; then I wondered
        &quot; <b>what the hell am I doing here&quot; </b>Those were not good
        days.&nbsp;</font>
</p>
<p align="left"><strong>
        <font face="Verdana">Questions: Have you
            heard that the 2nd Armored is being resurrected?</font>
    </strong></p>
<p align="left">
    <font face="Verdana"><strong>Answer: </strong>Yes it is,
        for use by the National Guard units. this reported in December that they will be
        a part of the III Corps at either Ft. Carson, CO. or Fort Riley, KA., both under
        the command of the III Corps. also the 24th Infantry is under similar orders.
        They will be under the command of a 2 Star General. This a new and changing
        army.</font>
</p>
<p align="left">
    <font face="Verdana">Question: Did you take basic training like
        the other men that entered the army ?</font>
</p>
<p align="left">
