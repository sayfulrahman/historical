        <strong><u>Old Glory:</u> THE OLD GLORY STORY<br>
        </strong>To veterans throughout American history, the Stars and Stripes has
        served as a symbol of their service and as a continuing testimony that the
        service is worthwhile. Since many flags of early America contained stripes
        in their design and several others had stars, there are varying accounts of when
        and where the first Stars and Stripes was flown. Flag history
        experts agree, however, that the first Stars and Stripes flag to have the
        general form we recognize today did not appear until the summer of 1777, when
        the Continental Congress formally resolved: &quot;That the flag of the United
        States be 13 stripes, alternate read and white, that the union be 13 white stars
        in a blue field representing a new constellation. Interestingly, the Congress
        did not specify the arrangement or shape of the stars, the direction of the
        stripes, or the relative size of the various components Consequently,
        military units and civilian public alike flew a confusing array of local, state
        and home-made interpretations of the Congressional flag description. It was not
        until 1912 that the flag was finally assured a uniform appearance, when
        President Taft signed an executive order prescribing the relative size, shape,
        and positioning of the Flag's components. A subject of almost as much debate as
        the shape ad design of the flag has been the question of how and when to display
        the Stars and Strips.
    </p>
    <p>
        Since 1942, when a law was first enacted to govern actual
        display he flag, the guiding statement has been that the flag should be flown on
        &quot;days when weather permits.&quot; The generally excepted
        interpretation was that the colors could be shown only between sunrise and
        sundown, and, even then, not during inclement weather. Growing sentiment that
        &quot;Old Glory&quot; should be kept flying irrespective of darkness and foul
        weather culminated in 1976, when President Ford signed legislation providing
        that&quot; &quot;When a patriotic effect is desired, the flag may be
        displayed twenty-four hours a day if properly illuminated during the hours
        of darkness.&quot; The new law also permits the Stars and Stripes to
        remain flying through inclement weather when a flag of all-weather material is
        used. In addition to its historical significance for permitting the
        national colors to remain aloft indefinitely for patriotic effect, the 1976
        legislation may be remembered for giving life to &quot;Old Glory&quot;.
        The new flag code contains the provision that&quot; &quot;The flag represents a
        loving country and is itself considered a living thing.&quot;. &quot;Its A
