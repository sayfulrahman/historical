        <strong><u>A Military Heritage: </u>( The Internet ) </strong>Many
        people assume the Internet is the brainchild of Silicon Valley. Actually, the
        driving force behind the Internet's creation was the <strong>U.S. Military </strong>during
        the Cold War. 4 October 1957, launch of the satellite<em> Sputnik </em>convinced
        America that its technologies needed beefing up. A key deficiency, military and
        civilian forces couldn't communicate during and after a nuclear war. To find the
        answers to this and other related issues, The Department of Defense launched the
        Advance Research Project Agency (ARPA) to conduct high-tech research. At the
        time, computers were generally seen as big calculators. Not so for one J.C.R.
        Licklider. An MIT professor and one of the ARPA'S early leaders, he envisioned a
        web of inter-connected computers feeding information to each other. Many could
        be destroyed in a nuclear attack, and still the larger communications system
        would remain intact. The idea became a reality in 1969 when four universities
        involved in ARPA projects had their computers connected. By 1972, they were
        sending e-mail to each other. The Internet was born. For more on the military's
        role in creating the Internet, read: &quot; The Ancient History of the
        Internet&quot; by Edwin Diamond and Stephen Bates in American Heritage, October
        1995; and &quot;Where Wizards stay up late&quot;, and ** &quot;The Origins of
        the Internet&quot; by Katie and Matthew Lyon, Simon &amp; Schuster, 1996. **
        Check our book store for this one. &quot;Its a Fact&quot;
