        <b>Lady Be Good: New update12 April
            2000. By Maj. Dale Rosenburg , Air Combat Command<br>
        </b><br>
        I was watching the history channel the other day and ran across a very
        interesting documentary entitled, &quot;Ghost Plane of the Desert: Lady Be
        Good.&quot; It focused on the disappearance, and later discovery, of a B-24D
        Liberator lost during World War II.<br>
        The aircraft departed from a base in Soluch, Libya, in April 1943, as part of a
        25-aircraft package intending to strike Naples, Italy. About three-quarters of
        the way to the target, the group aborted the mission due to poor weather. When
        the aircraft aborted, they broke formation and came back as single ships. All
        the aircraft returned, except the Lady Be Good.<br>
        The last known radio transmission from the Lady Be Good was a request to the
        home field for a radio course indication. After being several hours overdue, it
        was assumed the Lady Be Good was down, and search aircraft were dispatched to
        search for wreckage at sea. None was ever found, so it was assumed the Lady Be
        Good had crashed and sank with her crew.<br>
        In 1959, a British Petroleum oil survey crew was looking for oil in the African
        desert 440 miles from Soluch, Libya, where the Lady Be Good had been based. They
        discovered the wreckage of a B-24 which proved to be the Lady Be Good.<br>
        The Air Force dispatched investigators who made some startling discoveries.<br>
        First, none of the crew was in the Lady Be Good when it crashed, nor were their
        remains anywhere near the aircraft. Second, the navigator made no log entries
        after the mission was aborted. Third, the navigator's equipment and charts were
        stored in their cases. At this point, investigators concluded the navigator
        stopped doing his job shortly after they turned back for home.<br>
        Other bomber crews had reported higher than normal tail winds that night, but
        because the Lady Be Good's navigator wasn't doing his job, they flew right over
        their home field and kept on going. Lost and out of fuel, the crew bailed out.
        Several years later BP surveyors located five of the nine crewmembers' remains
        80 miles from the original crash site.<br>
        Surveyors discovered a diary on one of the crew that cleared up many mysteries.
        It told how one crewmember was killed instantly when his parachute failed to
        open. Daily entries described the harsh African desert conditions and the fact
        they had only one-half canteen of water for all of them, and no food.<br>
        Bottom line, the crew tried to walk out of the desert and covered 80 miles in
        eight days before they perished. It would be several years before two more of
        the crew were found about 25 miles from the others. The final crewman has never
        been found and is believed buried by years of blowing sand dunes.<br>
        So why do I tell this story and what lessons can we learn from it? Probably the
        biggest lesson is one that has literally thousands of examples in history:
        complacency has caused more accidents and fatalities than most diseases.<br>
        In the case of Lady Be Good, the crew got complacent about their jobs. Once they
        turned back they were so relieved there was not going to be any shooting that
        turning 180 degrees back to home base would be a cake walk. They let down their
        guard, and in the middle of the biggest shooting war in history, died not from
        bullets or anti-aircraft fire but rather from complacency.<br>
        For those of us in the aircraft maintenance business, there is no more insidious
        enemy than complacency. A crew chief looks over the same airplane day in and day
        out, sometimes several times a day. He or she follows the same checklist until
        they know it by heart.<br>
        How many of you check your car's engine oil at every gas fill up? You figure it
        was good before, so it's good now. And pretty soon you get to the point where
        you never check it; you just wait until the next change is due. You are
        complacent. If your car motor runs out of oil you will need a new engine, but
        it's not life or death.<br>
        However, if an F-16 aircraft engine runs out of oil, most likely the pilot is
        going to have a nylon letdown (ejection).<br>
        My point is you must balance the possible consequences of your actions or
        non-actions with the seriousness of the work to be done. Anytime something
        becomes routine, be aware. Complacency can sneak up and bite you.<br>
        Complacency can have catastrophic effects not just in the aircraft operations
        and maintenance world, but anywhere. The best way I have found to avoid
        complacency is to be aware. Pilots use a term called situational awareness, and
        that is what I mean by being aware.<br>
        Understand the seriousness of what you are doing and what can happen if it isn't
        done right, even if it seems routine. And if you're not sure about something,
        ask. The answers and experts are out there. (Major Rosenburg is the 57th
        Component Repair Squadron commander, Nellis AFB, Nev.)<br>
