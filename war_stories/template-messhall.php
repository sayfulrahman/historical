<div class="MsoNormal" style="MARGIN: 0in 0in 0pt">
    <font face="Verdana">We had a pretty good armored battalion except in one area,
        and that was a mess section that seem to believe that the only time they were
        required to serve edible food was only on Thanksgiving and Christmas. The rest
        of the year they were a.w.o.l. when it came to decent rations. Despite the
        efforts of the battalion commander to improve the over all quality of the chow
        things remained the same. Then one day Gen. Patton visited our unit and
        decided to eat with us. I was standing in the chow line with a fellow N.C.O.
        and happen to turn around when he and the battalion commander entered. I said
        to my to my friend “hey Loucks, look who'se here. I can't wait until he
        gets a mouthful of this swill!” (He had never sampled the culinary delights
        that awaited him from our mess hall) We grabbed a table where we would have a
        clear line of sight and yet be discreet to what would be a most entertaining
        lunch hour. “Loucks, look! He's going to the soda dispenser instead of
        getting kool-aid.” This machine would dispense the vast majority of the time
        two types of “drinks.” Either you would receive a cup full of syrup or an
        equally disgusting cup of foam. The General went forth and received a blast of
        carbon dioxide for his efforts. He held up this cup of foam and showed it to
        our increasingly unhappy Lieutenant Colonel with an expression on his face
        that only could be read as “what the hell is this!” They proceeded to a
        table and started to eat. Patton took a mouthful of some type of beef and
        rice. A most amazed and disgusted expression came across his face followed by
        some comments that I couldn't hear, but could well image the content. He ate
        about half of that slop, then picked up his tray, set it on top of our
        battalion commander's and walked out of the mess hall with him following
        with these trays. Loucks and I were doing everything to keep from bursting out
        with laughter. I felt sorry for my colonel, for he was a fine officer and
        really didn't deserve such a sorry mess section in his command. At the same
        time I wish to make clear that I was <i>not</i> laughing to myself at Patton
        or the colonel during this unfolding drama. Instead, I was thinking about what
        I knew about Patton from my own observations and those made by others who
        served with him in the 11th Armored Cavalry Regiment in Viet Nam. I knew that
        this sorry state of affairs wasn't going to be tolerated.</font>
