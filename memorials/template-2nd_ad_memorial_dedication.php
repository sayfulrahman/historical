<p>
<h2>
    <font face="Verdana">On Liberation</font>
</h2>
<font face="Verdana"><b>
        By Kathy Lally<br>
    </b>
    <br>
    <b>
        &nbsp;&nbsp; GELEEN, The Netherlands<br>
    </b>
    <br>
    &nbsp; On a bright afternoon this fall, as a few hundred people from this
    small town gathered on the green, I watched a fading memory start its
    transformation into a permanent memorial. After 60 years, private tales of
    liberation from Nazi occupation that had been told around family tables -- or
    sometimes not told at all because of the pain they evoked -- began to pass
    into the public realm.<br>
    <br>
    &nbsp; Arno Bemelmans had brought me on this journey. He was 18 years old when
    America's 2nd Armored Division -- known as &quot;Hell on Wheels&quot; --
    rolled into combat in this Dutch town on Sept. 18, 1944, their big Sherman
    tanks heralding the liberation of Limburg province, where Geleen is located.<br>
    <br>
    Years later, with the memory of that day still powerful, Bemelmans began a
    quest to know the names of the men who gave their lives in exchange for his
    freedom. &quot;I hated that they were anonymous,&quot; he told me one evening
    as we talked in his living room, &quot;that they passed by, and we didn't know
    who they were.&quot; It took Bemelmans 20 years to find the names of 61
    American soldiers who died liberating this thumb of land wedged between
    Belgium and Germany. One of them was my uncle.<br>
    <br>
    When I discovered last summer that the citizens of Geleen and neighboring
    Sittard were putting up a stone monument in remembrance of their liberators, I
    was startled. Hadn't Europe grown critical of America, angry over the war in
    Iraq, suspicious of a United States that many saw not as a benevolent
    superpower but as an arrogant bully? Hadn't this in turn provoked resentment
    in America and mutterings about European ingrates forgetting World War II?
    Now, those Europeans were inviting me to share in their remembrance of an
    uncle whose memory was almost lost to me. I knew I had to be there, not only
    in tribute to my uncle but to explore something about America, what the
    America of World War II shared with the America of Iraq and how they differed.<br>
    <br>
    &nbsp; My father's brother, Joseph M. Lally, was a private first class in the
    armored infantry. Our family was never sure exactly how he died. An Army
    officer said a German sniper shot him as he got out of his vehicle. After the
    war, a buddy of Joey's murmured something about his being burned alive in a
    tank. A photo of a handsome young man in a uniform always sat on my
    grandparents' television set, but no one in the family ever talked much about
    him. He had died at 21, with no wife or child to remember him. In the
    Cleveland of the late 1950s, I grew up thinking of him as a victim of the war.<br>
    <br>
    &nbsp; Arno Bemelmans told me he grew up thinking otherwise. He remembers
    venturing out of his house and across the fields that Sept. 18 after hearing
    news that the Americans were coming. Encountering forward units of the U.S.
    2nd Armored Division, he tried out his English for the first time. The
    soldiers asked where the Germans were. Bemelmans pointed out the way.<br>
    <br>
    For many years, he and his countrymen were immersed in rebuilding. Houses,
    factories and roads had been destroyed. The country had suffered deeply, with
    people killed not only by Germans but by British and American mistakes. In
    February 1944, U.S. warplanes accidentally bombed the town of Nijmegen,
    killing 200 civilians. Bemelmans and others told me they didn't blame the
    Americans, only the Germans and the evil of war.<br>
    <br>
    &nbsp; For Bemelmans, Uncle Joey and his fellow Americans were heroes.
    &quot;They left their homes to bring us peace and liberation,&quot; he said in
    his now fluent English. &quot;These brave men fell far from home, never to
    return to the home of the brave.&quot;<br>
    <br>
    &nbsp; That conviction drove Bemelmans to campaign for the monument that would
    carry the names of the liberators onward into the future. He is 78 now. No one
    with a memory of the liberation is young anymore. The personal recollections
    are dying out as surely in Geleen as they are in Cleveland. Bemelmans wanted
    to capture as much of the events of 1944 as he could, before they were lost to
    memory altogether.<br>
    <br>
    &nbsp; American privacy laws complicated his task. He persisted, with help
    from a retired Connecticut police detective whose son happened to be a U.S.
    Special Forces officer assigned to NATO and living next door to Bemelmans, and
    whose other son lived near a member of Congress, Rep. Rodney Frelinghuysen, in
    New Jersey. &quot;I told my staff, 'We're putting on a full court press to
    identify the next of kin,' &quot; says Frelinghuysen, a Vietnam veteran whose
    ancestors came from Holland -- in 1720. &quot;I told them, 'If they're working
    so hard in the Netherlands, we're going to work just as hard on this side.'
    &quot; A letter from Frelinghuysen's staff to the Cleveland Plain Dealer had
    brought me to the green where Bemelmans and the people of Geleen and Sittard
    unveiled the monument on that sunny Saturday.<br>
    <br>
    The memorial is shaped like an H, to stand for Hell on Wheels. &quot;Too
    macho,&quot; says Bemelmans, who prefers to think of the H as representing
    &quot;herinnering,&quot; Dutch for remembrance, and &quot;herdenking,&quot;
    Dutch for commemoration. He wants English speakers to think of
    &quot;homes,&quot; for the ones the soldiers left behind.<br>
    <br>
    &nbsp; NATO sent a band to the ceremony. The United States sent a brigadier
    general, and several servicemen bearing the American flag and a wreath to
    honor the dead. One sailor told me he felt honored to be there. These days, an
    American in uniform can feel uncomfortable in Europe, he told me. Many see
    only the uniform of the country that invaded Iraq.<br>
    <br>
    &nbsp; A color guard from an ancient Dutch regiment raised flags and fired a
    salute as the monument was unveiled. Aging veterans of the Dutch partisan
    movement stood at attention in their maroon berets. Bemelmans read the names
    of the dead, pausing part way through as emotion overtook him. The band played
    &quot;America the Beautiful.&quot;<br>
    <br>
    &nbsp; Uncle Joe was 14th on the list. When his name was read, Wim van der
    Linde rested a gentle hand on my shoulder. Van der Linde, who was 16 when the
    Americans came to Geleen, had invited me and my husband to stay with him and
    his wife, Toos. The day before, they had taken us to Margraten, where 8,301
    headstones mark the graves of Americans who died here. Like many Dutch, the
    van der Lindes still lay flowers at those graves. &quot;We still feel very
    grateful for what the American soldiers did for us,&quot; Toos told me.
    &quot;It's very difficult to show that. This is one way. I still wonder about
    all the parents who were living in a peaceful place and then someone came and
    said to their sons, 'You, you and you, go to Normandy.' And they did. I still
    find it amazing.&quot;<br>
    <br>
    &nbsp; Walking among the crosses and the Stars of David, I found myself
    reading the names, wanting to acknowledge them, just as I do when the names of
    casualties in Iraq are printed in the newspaper. The war in Iraq has troubled
    me, filled me with doubts about my country and its actions, but I mourn the
    men and women dying there, and I read their names.<br>
    <br>
    &nbsp; &quot;Those were the names,&quot; Bemelmans said after his own reading,
    &quot;lest we forget.&quot;<br>
    <br>
    &nbsp; Then he brought out a ragged 48-star American flag, which, like the 61
    soldiers, had come to the end of its journey 60 years ago here in the southern
    corner of the Netherlands.<br>
    <br>
    &nbsp; Uncle Joe died on a long hill leading to a place called Kruizberg. He
    wasn't fighting, really, for the liberation of the people of Geleen, but in a
    larger battle against fascism in Europe and beyond. Bemelmans understands
    that. He understands, too, how the sweep of great armies in epic struggles
    nonetheless catches up specific people. One young man in a field pointed
    toward the Germans that September and went on to live a full and rewarding
    life in a democratic Europe; another, the son of Irish immigrants in
    Cleveland, died there.<br>
    <br>
    &nbsp; After the ceremony was over, I talked to two Dutch fathers who had
    brought their 13-year-old sons to lay a wreath. I asked the fathers how
    today's America differs from the one of 60 years ago. &quot;Today, it's a very
    different question morally,&quot; Mark Daemen told me. &quot;Will the Iraqis
    be putting up a memorial in 60 years?&quot;<br>
    <br>
    &nbsp; All liberations are not the same. Memory and memorial, too, are in the
    hands of those who survive, colored by what comes after. Uneasy though some of
    us may be about Iraq, there is still time to shape what will come. Memories
    will depend on how America conducts itself in the years to come, not just in
    Iraq but around the world. We owe the young men and women who are dying today
    remembrance, and an ending to the war that will allow history to treat them
    not as victims but as liberators.<br>
    <br>
    &nbsp; When World War II had been won, the 2nd Armored Division returned to
    Fort Hood in Texas. Eventually, in 1991, as the Army scaled back, it was
    deactivated there.<br>
    <br>
    &nbsp; My cousin, Betty Lally Kraynik, had been planning to go to the ceremony
    for our Uncle Joe. Instead, my husband and I went without her. Betty was in
    Fort Hood, now home to the 1st Cavalry Division. The day Geleen commemorated
    the soldiers who gave their lives there in 1944 -- the day it dedicated a
    public memorial to a collective event greater than any single memory can
    encompass -- Betty was at the funeral of her brother-in-law, a 36-year-old
    Army staff sergeant from Youngstown, Ohio, who was killed in Iraq as he
    emerged from a tank.<br>
    <br>
    &nbsp; And now I offer you his name to read.<br>
    <br>
    &nbsp; Elvis Bourdon.
</font>
