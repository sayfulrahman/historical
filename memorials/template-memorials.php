    <p>
        <font face="Verdana"><b>Mountjoy, Jesse T., 2nd Lt.:</b> Was the pilot of a P-47, Thunderbolt, on a
            strafing and bombing mission over the French village of La-Haye-Pesnil, and was
            shot down on 31 July 1944. . He was a member of the 405th Fighter Group of the
            509th Fighter Squadron. La-Haye-Pesnil is SSE of Granville, France. Lt. Mountjoy
            was from Horse Cave, Kentucky, and was known around the Lexington, Kentucky
            area. He had received the Army Air Medal, four Oak Leaf Clusters, , He received
            his wings on August 30, 1943. After all these years almost 56 years later they
            did not know where he was from just his name, now they know, another French
            village honors an American hero. The village will have a dedication of a
            memorial honoring him on the 12 November 2000. At the time of his death, he and
            his wife Mrs. Runelle Palmore Mountjoy were the parents of a 18 month old son.
            That son now lives in Utica , Kentucky,&nbsp; his name is Jesse T. Mountjoy. This
            memorial has been dedicated, his son did visit France this summer and
            attended a service. Completed</font>
    </p>
