    <p>
        <font face="Verdana"><b>Markle, Donald J. 2nd Lt. O-694170,</b> 486th Fighter Squadron, 352nd Fighter
            Group. Entered the Service from: New Jersey Died: June 10, 1944 Buried at: Plot
            J Row 12 Grave 20 Brittany American Cemetery St. James, France His plane a P-51,
            Mustang went down while on a strafing run near a small village in Normandy by
            the name of La Besliere, France. ( this is southwest of St. Lo., France.) Lt.
            Markle was from the vicinity of Dunellen, NJ. He did attended high school at
            Dunellen, NJ. , he was born Oct 22, 1922 in Plainfield, NJ. , and entered
            Rutgers university , Sept. 1941 then received an Air Force commission on 10/1/43
            as a pilot. . After an intensive search with the help of the editor of the local
            newspaper we were able to locate relatives of Lt. Markle. We must say we were
            overwhelmed at the calls and email that we received from the citizens of
            Dunellen and vicinity. Dunellen has much to be proud of, everyone was very good
            to us, especially his 1940 graduating class , we did talk to a few of them, what
            a breath of fresh air, wonderful people and we thank them from the bottom of our
            heart. A memorial dedication ceremony is to take place in this small village on
            22 April 2001 the relatives are going to France for the dedication. We also want
            to thank Major Ken Dungey and his wife Terry ( St. Denis Le Gast ) for all they
            do for the veterans we left behind in France, and also for the assistance they
            give to all veterans that come their way. This memorial has been dedicated and
            his living relatives did attend in May of 2001.&nbsp;</font>
    </p>
