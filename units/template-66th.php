                        1946. In June 1946, those of us that volunteered were sent to Task Force Frost
                        to test experimental clothing and equipment under extreme cold weather
                        conditions at Camp McCoy, Wisconsin where temperatures went as low as minus
                        35-40 degrees. We remained there from June 1946 to April 1947. Along with us
                        were elements of the 41st Armored Infantry as well as elements of the 17th
                        Armored Field Artillery and the 82nd Armored Recon.<br>
                        &nbsp;&nbsp;&nbsp;&nbsp; Elements of the 66th and 67th Tank Bns. were dent to
                        Anchorage (Task Force Frigid) and the Aleutians (Task Force Williwaw). Task
                        Force Frost ended April 1947 at which time we returned to Camp<br>
                        Hood. When we left in 1946, the Camp was a full division but, when we returned,
                        the camp was virtually deserted. I'm not sure when Frigid and Williwaw
                        terminated but, I understand, that Williwaw was a disaster. In your bio of the
                        2nd, no mention is made of the 1945-50 period; it ought to be covered. Those
                        winter field tests did produce some of the gear later used the Korean War. It
                        was important. Thanks,&nbsp; ( This information was sent in by an interested
