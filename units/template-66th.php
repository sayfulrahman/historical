                        fought under the designation &quot; The 6th Tank Battalion&quot;. During the
                        war, winning seven battle streamers and the Korean Presidential Unit Citation.
                        These honors were awarded to the 66th Armored Regiment when the sixth Tank
                        Battalion was deactivated after the conflict.&nbsp;&nbsp; Quote:&nbsp; &quot;<i>I
                            am formerly a member of the 2 AD from Jan 1973 through Jan 1976 and I have some
                            significant information which might correct some misquoted dates and other
