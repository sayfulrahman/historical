                </p>
                <p><i>
                        <font face="Verdana">At the time that I arrived as permanent party to the 2
                            AD, in March 1973, armored battalion level elements of 2 AD on station at Fort
                            Hood were 1st &amp; 2nd Bn, 66th Armor and 1st &amp; 2nd, 67th Armor. I believe
                            it was 2/67 that was assigned the experimental M-60A2 tanks, [&quot;A&quot;
                            Duces as I was told], which remained with that unit until I was discharged in
                            1976. My first assignment was to A Co., 1st Bn, 66th Armor [with the 1st Platoon
                            and later to the Headquarters tank section of that company on the commander's
                            tank.] Under two company commanders and for almost a year and a half, I spent
                            most of my time in the field on a wide variety of training missions which
                            included two annual gunnery tests, a host of field maneuvers and a week long
                            stint in civil disturbance training. [From your own remarks, and my own
                            experience, it would appear that some vestige of Task Force 11 was either
                            reactivated or kept as a planning unit during the waning days of the Vietnam war
                            as late as 1974.]</font>
                    </i>
                <p><i>
                        <font face="Verdana">In late 1974, I was detached to B Company 1/66 Armor
                            which was to TDY&nbsp; for a special three month training duty at Hunter-Liggett
                            Military Reservation. The unit was flown by charted jet to Monterey, California,
                            trucked to Liggett and billeted on a remote part of the reservation in near
                            isolation even from permanent party staff at the reservation. Over the course of
                            this three month period, B Company 1/66 Armored acted as an aggressor force,
                            posing as a Soviet tank force and largely serving as live targets for field
                            trials of the Hell Fire and extended TOW missile systems.</font>
                    </i>
                <p><i>
                        <font face="Verdana">By the time I returned to Fort Hood, I found myself
                            assigned to a &quot;bastard&quot; company attached to the 1/66 Armor, so-called
                            &quot;D&quot; company for approximately six weeks. D Company apparently started
                            with nine enlisted men, two officers and three NCOs and no equipment whatsoever.
                            During that time, rumors abounded over the purpose of this &quot;bastard&quot;
                            unit until the newly assigned company commander announced our unit designation
                            was soon being changed to A Company 3rd Battalion, 67th Armor. Shortly there
                            after, our ranks swelled rapidly as new personnel filed in almost daily, mostly
                            raw recruits straight from basic training and cast off deadbeats from other
                            units which were being reassigned to Germany. This combination of personnel was
                            a sure fire mix to make us finish dead last in every category the US Army could
                            conceive of at the time. Worse, when the newly formed 3rd Battalion 67th Armored
                            was issued its equipment, we were actually signing off 1st Battalion 67th
                            Armor's gear wholesale because they were being TDY sent to Germany for a six
                            month rotation. The vehicles and much of the immovable equipment was stripped
                            and practically worthless to a combat unit; however, Division's orders were to
                            assume the equipment and get it all in shape for gunnery exercises in four
                            month's time. What Division didn't tell us was that 3/67 was largely a &quot;low
                            priority&quot; unit for the duration of the unit level rotations to Germany.</font>
                    </i>
                <p>
                    <font face="Verdana"><i>From my recollections, 1st 67th Armor, 1st or 2nd 50th
                            Infantry and a signal unit [52nd?] were relocated to Germany during the first
                            rotation from roughly February, 1975 through August 1975. A battalion of the
                            66th Armor, and other sundry support units, followed during the second rotation
                            shortly before I was discharged from service. Friends of mine in both 66th and
                            67th Armor later told me that each unit in the 2 AD rotated to Germany for six
                            month stints at a time until all units had spent brief tours on European soil.
                            Apparently this was the US Army's means of bolstering NATO unit strengths
                            without the spousal and dependent baggage that frequently follows GI's in
                            assignments around the world. Two of my friends actually PCSed to Germany
                            independent of 2 AD's&nbsp; unit rotations and gave me some interesting accounts
                            of some of these units on temporary overseas deployment after my own discharge.</i>
                        <i>Your reports of 67th Armor's deployment in 1978-79 would suggest to me that
                            this unit rotation scheme, or some variant of it, was still being conducted far
                            longer than I would have suspected.</i><br>
