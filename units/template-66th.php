                <h1><strong><a name="66th Armored Regiment:">
                            <font face="Verdana">66th
                                Armored Regiment:</font>
                        </a></strong></h1>
                <p>
                    <font face="Verdana">The 66th Armored Regiment is the oldest Armored
                        unit in the United States Army, tracing it's lineage to the beginning of the
                        Tank Service in February 1918. The Regiment participated in the battles of St.
                        Mihiel, France, where it received it's baptism of fire in the Meuse-Argonne and
                        the Somme Offensives.</font>
                </p>
                <p>
                    <font face="Verdana">The Coat of Arms of the 66th Infantry tells the story of
                        battle and high distinction won on the Field of Honor. On the shield of the
                        armored knight, which we Tankers are proud to bear, are the colors of the
                        Infantry , on the right, the Artillery, on the left, and the Cavalry below.
                        Volunteers from these combat branches manned the first American tanks fighting
                        in France eighty-four years ago. The 301st and 344th Tank Battalions were the
                        parent organizations, and mashed the line at Hindenburg on the fought at Somme,
                        St. Mihiel, the Argonne Forest. During this action the 344th Battalion led the
                        advance of eight different divisions and was in action for 46 consecutive days.
                        The experienced officers and men who formed the original three regiments of the
                        2nd Armored Division were drawn from the 66th Infantry, the nation’s only tank
                        regiment in 1940. These three regiments were concentrated in the 2nd Armored
                        Brigade, the division’s striking force, and placed under the command of
                        Brigadier George S. Patton, Jr. who as a Colonel had commanded one of the
                        battalions which provided the bulk of the cadre of officers and men for the 66th
                        Infantry.<strong> </strong>Although numerical designation of the regiment was
                        changed several times during the period of 1918 to 1928, its unity, integrity,
                        fighting spirit, gallant leadership and esprit de corps remained unchanged. On
                        15 July 1940, the unit was re-designated from the 66th Infantry (Light Tanks) to
                        the 66th Armored Regiment as part of the newly formed 2nd Armored Division,
                        stationed at Ft. Benning, Georgia. In December 1942, the 66th as a part of the
                        2nd Armored Division participated in the amphibious invasion of French Morocco
                        in North Africa and was a part of the Division's triumphant entry into
                        Casablanca. The regiment participated in the invasion of Sicily and through
                        fierce fighting earned the 66th six battle streamers during the War.</font>
                </p>
                <p>
                    <font face="Verdana">In 1944, the 66th went into action on the
                        European front, landed on Normandy’s beaches on 9 June 1944. A week later the
                        66th as a part of the 2nd Armored Dvision decisively defeated the German 6th
                        Parachute Regiment and the 37th SS Panzer Regiment near Carnetan, France. The
                        66th moved across France to the German border. It was diverted southeast to
                        counter the German advance during the battle of the Bulge, assisting in the
                        destruction of the 2nd Panzer Division and assisted in capturing Hoffalize,
                        Belgium.<strong> </strong>As a result of it's exemplary conduct in the
                        liberation of Belgium, the Regiment was twice cited in the Order of the Day of
                        the Belgium Army. The Second Armored Division was selected to occupy Berlin and
                        serve as the first American troops to enter the fallen German Capital. In
                        World War 2 one of many noted battle of the 66th was in the vicinity of
                        Geilenkirchen, Germany. This started on 18 Sept 44 attacked and breached the
                        Siegfried Line in the vicinity of Ubach, 3 Oct 44. after one of the greatest
                        tank battle of the European Campaigns, this was fought on 20 Nov.44, reached the
                        Roer River, 28 Nov. 44. The 82nd Recon had a limited role in this tank battle.</font>
                </p>
                <h2>
                    <font face="Verdana">66th Armored Regiment: Historical documented
                        facts.</font>
                </h2>
                <p>
                    <font face="Verdana"><strong>1944-1945: </strong>On 30 July 1944 the 2nd
                        Battalion, which had been in reserve, attacked and fought for thirteen hours to
                        secure and hold the Villebaudon, Fr. crossroads. The enemy resisted vigorously,
                        counter-attacking with tanks and infantry, and throwing in heavy concentrations
                        of artillery. In one counter-attack, the enemy employed 40 tanks. Elements of
                        the 702 Tank Destroyer Battalion, attacked, knocked out eleven German tanks
                        during this action and the preceding two days, one hundred and eighty of the
                        enemy were killed and one hundred and twenty were captured. The 1st Battalion,
                        66th Armored Regiment was on thirty minute alert to support the 2nd Battalion,
                        but was not called upon. On 31 July 1944 the 1st Battalion, 66th Armored
                        Regiment attacked along the axis of the Villebaudon-Tessy-sur-Vire road and
                        reached a point immediately south of Beaucoudray, before encountering a ravine
                        defended by enemy tanks in strength. On 1 August 1944 the 3rd Battalion, 66th
                        Armored Regiment on the north and the 1st Battalion, 66th Armored Regiment on
                        the south of the Villebaudon-Tessy-sur-Vire road, attacked. The 3rd Battalion,
                        66th Armored Regiment had its column cut by five Panther tanks. The battalion
                        fought them off and pushed on the the outskirts of Tessy-sur-Vire. The were
                        preceded by Troop &quot;A&quot; of the 24th Cavalry Reconnaissance Squadron,
                        which had arrived at the edge of town, but had not attempted to seize it. Two
                        tanks were sent into town but were destroyed. The attached infantry from the
                        22nd Regimental Combat Team were then sent into Tessy and secured it by 1500B.
                        The 3rd Battalion, 66th Armored Regiment had attacked that day with only twenty
                        four tanks, less than half of its strength, and lost 14 of that number in the
                        attack. The 1st Battalion of the the 66th forged its way to within about 1000
                        yds. of the town, knocking out five enemy tanks and capturing 60 prisoners. At
                        1700B, the 2nd Battalion , 66th Armored Regiment, which had been held in
                        reserve, was committed and in an attack which ended about a mile west of Percy
                        knocked out seven enemy tanks and captured 70 prisoners. Later that night the
                        30th Infantry Division occupied Tessy, and the gap between Tessy and the 1st and
                        2d Battalions of the 66th Armored</font>
                </p>
