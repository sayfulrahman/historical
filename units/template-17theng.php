<p><font face="Verdana">At Ciney, Belgium, ( Ardennes Offensive ) Gen. Collier was
in command of CC &quot;A&quot; was ordered by Gen. Harmon to hold Ciney, in this
force was 2nd Bn., 41st Armored Inf., 2nd Bn. 66th Armor, and a platoon each
from &quot;A&quot; Co. 17th Engineers, and &quot;A&quot; Co. 702d Tank Destroyer
Bn.. On November 26, 1944 along with other elements of CC &quot;A&quot;, Co.
&quot;A&quot; 17th Armored Engineers was ordered to secure Merzenhausen and
Barmen to cut off communication lines to the Roer.</font></p>
<p><font face="Verdana">August 1944: Seine River Crossing: After pushing a
bridgehead force across, Company &quot;A&quot;, 17th Engineer Bn., built a
thirty-foot treadway bridge. Gen. Collier ordered them across before nightfall.</font></p>
<p><font face="Verdana">17th Engineers: A Narrative Report of Company
&quot;D&quot;, for early August 1944, It goes like this : In assembly area at
Notre Dame De Cenilly. 2 August 44: Moved to Percy, 3 August 44: Moved one mile
west of Montbray. 4 August 44: Moved from Montbray to one mile south of Montbray
where enemy was contacted and company with reconnaissance elements ( 82nd
Reconnaissance ) were forced to withdraw. 5 August 44: Moved to one and one-half
miles east of St. Sever De Calvados. Removed twenty # 2 and # 4 Teller mines
from the roads and booby traps from the hedgerows. 6 August 44: Moved three
miles west of St. Sever De Calvados. 7 August 44: Moved to two miles west of
LeTeilleul. 8 August 44: Moved to two miles southeast of Lonlay L�Abbaye and
at 2300 blew bridge in center of town after having entered earlier in the
afternoon and then withdrawing. Also blew up enemy ammunition dump at this town
and destroyed four enemy vehicles. All this was done in conjunction with a
platoon of the 82nd Recon. Bn.</font></p>
<p><font face="Verdana">Hedgerow penetration: The 17th Engineers used a tank with
a bulldozer blade to go through hedgerows in France. But then the Germans would
wait until the tanks would cross with infantry following on foot then fire on
them. In order to counter this, the American forces started through the
hedgerows with their main guns loaded with canister and pointed to the rear and
to the flanks. As the tanks crashed through the opening , they fired parallel to
the hedges, inflicting great casualties. For this the 2nd Armored was given the
name of &quot;Roosevelt's Butchers,&quot; a name which the division gladly
accepted.</font></p>
