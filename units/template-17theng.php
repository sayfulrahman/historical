<p><font face="Verdana">24 March 1945: Co. &quot;A&quot; 17th Engr. Bn., attached
to XVI Corps, constructed a bridge across the Rhine River south of Wesel,
Germany. Co. &quot;A&quot; with Co. &quot;C&quot; 202nd Engineer Combat team
attached an 1153 foot M-2 treadway Bridge in support of the XVI Corps assault
crossing. The bridge site was four miles south of Wesel, Germany, was secured by
the 30th Inf. Div. The assembly of the bridge took place in a woods near Camp
Renuck, here bridge floats were prefabricated, it took 25 -2 1/2 tons trucks to
transport this unassembled bridge to the site. No direct fire was received on
the bridge site while under construction, but artillery was a problem from the
Germans. Five days later the whole division unleashed its striking power of
tanks, armored artillery and armored infantry to force crossings over the
Dortmund-Ems Canal, as well as the Weser, Leine, Innerste, Oker and Elbe Rivers.
Useful information from Germany.&nbsp; 4/82001</font></p>
<p><font face="Verdana">On 30 March 1945 a patrol from Co. &quot;D&quot;, 17th
Engr. Bn. attempted to investigate and cross the bridge across the Dortmund-ems
canal at Oleen, in the face of small arms fire. The bridge was blown up by the
enemy before it could complete it's reconnaissance. On the same day , Co.
&quot;A&quot; 17th Armd. Eng. in support of Co. &quot;C&quot;, constructed a
treadway bridge across the canal near Alvert.</font></p>
<p><font face="Verdana">On the 5 April 1945, the 17th Engr. Bn. constructed a 384
treadway across the Weser at Ohr, Germany, permitting CC &quot;A&quot; to cross
the river with armor and continue with it's operation.</font></p>
<p><font face="Verdana">On 8 April 1945 division engineers constructed a pontoon
bridge across the Weser Elbe Canal, west of,Harsum allowing CC&quot;A&quot; to
continue it's drive northeast to Braunschweig.</font></p>
