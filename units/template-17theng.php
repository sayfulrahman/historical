<p><font face="Verdana">17th Engineers: Prior to the 2nd Armored Division entry
into the Rhine-Elbe offensive and the drive across the Westphalian plain of
Germany through Bruswick and Saxony to the Elbe River. Co.E, 17th Engineer
Battalion, ( the division�s organic bridge company ) broke all records in
bridge construction by completing an 1152-foot treadway bridge across the Rhine
in approximately nine hours.</font></p>
