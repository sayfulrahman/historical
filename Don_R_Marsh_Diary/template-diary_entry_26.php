    <h1 align="center">
        <font face="Arial">ONE COLD MORNING</font>
    </h1>
    <p align="center"><i><b>
                <font face="Arial">By Don R. Marsh</font>
            </b></i></p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; It is always the coldest just before daybreak, that
            particular morning it was even colder than I could ever recall. The coldness of
            my feet troubled me almost as much as my hands, as my fingers were already numb
            from holding my gun at the ready.</font>
    </p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; I nervously chambered a round and clicked off the safety
            catch. Now it was wait time. Waiting for the expected moment of decision when to
            pull the trigger, hoping I wouldn't flinch from the expected muzzle blast and
            the stock toe slamming into my shoulder. Just trying to suppress the overanxious
            feeling became taxing. The adrenaline began pumping as the long awaited action
            was about to begin. We crouched in silence waiting for the first shot to be
            fired. Coping with the frigid temperature meant stamping my feet in the cold
            boots to maintain circulation.</font>
    </p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; My buddy Larry wise-cracked, &quot;Don't shoot until you
            see the whites of their eyes!&quot; I thought he's nuts if he thinks I'm going
            to sit and wait until they get that close. Their scouts were approaching as we
            all strained in the semi-darkness to pick up the leaders. The word was passed
            not to begin shooting until they were well within range. As I pressed my cheek
            to the ice-cold stock, the misty vapors from my rapid breathing momentarily
            clouded the rear sight blotting out the front sight.</font>
    </p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; I found myself repeatedly double checking to make sure
            that I had clicked the safety off to avoid that gut-wrenching feeling of pulling
            the trigger and having nothing happen.</font>
    </p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; Patience was never one of my virtues and at the last
            moment I leaped to my feet and fired at the leaders. Then all hell broke loose.
            Firing erupted all around me and the odor of cordite quickly filled the air. Off
            to my right, the others were firing too. Soon I could hear lead whizzing
            overhead from the cross fire.</font>
    </p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; I fired off all my initial rounds and quickly reloaded to
            resume shooting at the fleeing silhouettes. The barrel grew too hot to touch and
            suddenly as it began it became quiet as the firing died out. Over the ringing in
            my ears, I could barely hear the others' voices as we vacated our camouflaged
            holes to count the kill. Our small group were all &quot;old-timers&quot; and
            rather nonchalant about their achievements that morning.</font>
    </p>
    <p>
        <font face="Arial">&nbsp;&nbsp;&nbsp; The sun was rising on the start of another good hunt day
            at our private hunting club near the Salton Sea in Southern California.</font>
    </p>
