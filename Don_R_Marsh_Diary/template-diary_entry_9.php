<h1 align="center">
    <font face="Arial">
        TWO DIFFERENT WORLDS<br>
        (10 MILES APART)</font>
</h1>
<p align="center">
    <font face="Arial"><br>
        <i><b>By Don R. Marsh</b></i>
    </font>
</p>
<p align="left">
    <font face="Arial">Heerlen, Holland<br>
        10 November 1944</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Every year now when the late fall winds bring the shift of the cold north
        winds here to sunny southern California, my mind wanders back to a period of
        time when I was not exactly enjoying life in Uncle Sam's Army. I'm sure at one
        time or another we all have infrequent reminders of those lost days of our
        misspent youth. Don't get me wrong - the army wasn't all bad all of the time,
        just most of the time. Besides, it was what the army had me doing and where. On
        this one particular day it was bad, as a matter of fact, very bad. The time was
        November of 1944 and the place was just inside the Siegfried Line near the
        German village of Baseweiler.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Somewhere among the cobwebs of the Army archives for World War Two, some
        diligent clerk made an entry in my personnel record that I was then performing a
        mandatory function of laying field wire and maintaining the lines of telephone
        communication between our Headquarters of Combat Command &quot;A&quot; and our
        subordinate infantry, tank and artillery units. This esteemed position was not
        one of my choosing but rather pure luck, bad luck. As was repeated thousands of
        times over in classifying recruits, some damn clerk making life and death
        decisions on a typewriter inserted my name and MOS for assignment into the Table
        of Organization for this armored division, known as Hell On Wheels, the US 2nd
        Armored Division. How they managed to put the square peg in the round hole I'll
        never know, but here I was for the &quot;duration plus six months&quot; as it
        said on my enlistment form. A bitter lesson in be careful what you ask for.
        Regardless, I was not here of my choosing - Old Lady Luck had done it to me
        once again, known as the fickle finger of fate.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; One day during a lull, a couple of us including Doug Donahue, Larry Hull,
        Bill Veno and I decided to pay a visit to &quot;The Company.&quot; Because we
        were permanently assigned TDY (Temporary Duty) to Headquarters Combat Command
        &quot;A&quot;, we seldom had the opportunity to visit our &quot;company of
        record,&quot; this being a ledger entry on paper only, in the rear echelon. We
        lived in two different worlds, separated by a mere 10 miles at the time. This
        was an opportune time to locate them back in the city of Heerlen, Holland,
        across the border from Germany; safe from the sounds of gun fire. Bill Veno was
        the only old timer among us; Larry Hull and Doug Donahue and I, having been
        replacements were in reality strangers to the men and officers of this outfit.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; In retrospect, the men in the company were not hostile towards us, but
        just
        seemed cold and indifferent - as though we were outsiders who intruded on
        their private domain. In a sense, we were strangers, but fate had drawn us all
        together.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Appearance-wise, we showed up looking like a bunch of poor country cousins
        looking for a handout, which we were. By comparison, we were crummy looking next
        to these REMFs (Rear Echelon Mother Fuckers). Our OD trousers were worn, greasy
        and shiny from six months wear daily in the field since landing on the Normandy
        beach. Their trousers were exceptionally clean and new in appearance. Would you
        believe, some even had creases pressed into their pants! We had hoped to obtain
        winter gear, particularly overshoes, plus whatever else we could beg, borrow or
        steal. The chance of the latter was zilch as they watched us too closely.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Veno went to the kitchen to see the Mess Sergeant, nicknamed
        &quot;Mama&quot;
        due to his effeminate mannerisms. I thought it was ironic that these
        &quot;mothers&quot; would call their Chief Bellyrobber &quot;Mama.&quot; Bill
        Veno was trying to put the mooch on him for some real ground coffee bean coffee
        as we were drinking instant coffee daily. He got a couple pounds only after
        promising to loot a nice set of china for the Officers Mess from unoccupied
        German homes; the former occupants having fled across the Roer River.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Here these lucky characters in the Signal Company were enjoying three hot
        meals every day, hot showers when wanted at the nearby coal mines shower rooms
        for miners and sleeping in honest to goodness beds. Many of the guys were making
        out with the town's willing young women who had taken a shine to the new
        &quot;liberators.&quot;</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; I noticed the First Soldier, Robert Koch, strutting around like a peacock
        wearing a new tank jacket, an item in very short supply. Something else caught
        my eye; he was wearing a pair of new cloth covered galoshes to keep his tootsies
        dry and warm. That was exactly why I had taken the ride - for a pair of boots.
        Finding the Supply Room, I asked the tall Supply Sergeant Youngman for a pair of
        overshoes and he informed me that the company had already received their single
        shipment of boots and they were all issued and that he had no more coming.
        Something told me that &quot;my pair&quot; had already been sold on the
        black-market. When I asked how I was to obtain a pair of boots, he casually told
        me to try the nearby Graves Registration Collecting Point where they stripped
        the reusable equipment from dead soldiers. Reluctantly, this is what I was later
        forced to do under the circumstances. I found one size 10 boot in cloth and the
        other boot in rubber size 10, hoping they would bring me better luck than the
        previous owners.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Wandering around the Company area, the Wire Lieutenant, Nixon Mc Neil,
        spotted us and asked Veno, our Corporal in charge, what in the hell we were
        doing back there and not up in Germany where we were assigned. Veno explained
        that we had driven back to hopefully to draw badly needed winter gear only to
        learn that we were too late and missed the boat and could not be issued
        anything. This 90 day wonder, a former cab driver from Oklahoma City, was also
        the same asshole a month earlier who had requested Sergeant Earlie J. Jones to
        have the wire team &quot;obtain&quot; (steal) some piglets from the German farms
        to be eaten in their Officers Mess. Not being a dog-robber nor brown-nosed, I
        refused to even consider his asinine &quot;order&quot; to earn brownie-points. I
        thought; eat your damn Spam like the rest of us.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; All this was too much for me and I was eager to return to Krautland with
        my
        friends in Hqs CCA and take my chances. Other than scrounging some coffee, we
        were unable to neither mooch anything else nor find any cumshaw we could use.
        What we were asked repeatedly, &quot;Got any Lugers to trade?&quot; Other than a
        mutual desire to return home alive we had very little in common in our
        respective military experiences. They lived in a protected environment and I
        seriously doubt any of them had ever heard a shot fired in anger. Never
        explained to me was why our wire crew was not rotated with other crews in the
        rear, as was the standard operating procedure in other similar units. I believe
        it was the old army story of &quot;friends in higher places.&quot;</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Several years ago at a national Division reunion, one of &quot;them&quot;
        unknown to me came up and asked, &quot;Don't I know you?&quot; I just smiled and
        answered, &quot;No. I'm sure you don't.&quot;<br>
        The lyrics from one of Willie Nelson's ballads come to mind when he sings,
        &quot;Forgiving you was easy, but forgetting takes the longest time.&quot; There
        are some things in life one never forgets.<br>
        <br>
    </font>
</p>
