<h1 align="center">
    <font face="Arial"><span style="mso-spacerun: yes">&nbsp;</span>THE
        MUSTANG GENERAL - THE MAN FROM COLORADO</font>
</h1>
<p class="MsoSubtitle">
    <font face="Arial">By Don R. Marsh</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoSubtitle">
    <font face="Arial">A<span style="font-size:10.0pt;mso-bidi-font-size:12.0pt">n
            enigma: a person of puzzling or contradictory character.<o:p>
            </o:p>
        </span></font>
</p>
<p class="MsoSubtitle"><span style="font-size:10.0pt;mso-bidi-font-size:12.0pt;
font-weight:normal;font-style:normal">
        <font face="Arial">PART I<o:p>
            </o:p>
        </font>
    </span></p>
<p class="MsoNormal">
    <font face="Arial">13 June 1944</font>
</p>
<p class="MsoNormal">
    <font face="Arial">Carentan, France</font><span style="mso-spacerun: yes">
        <font face="Arial">&nbsp;</font>
    </span>
</p>
<p class="MsoNormal">
    <font face="Arial">My initial encounter with Pyramid Six,
        the Command Code Name for Brigadier General Maurice Rose happened near Auville
        in Normandy, France, on 13 June 1944, after I had been assigned to his
        Headquarters Command of Combat Command “A” of the 2<sup>nd</sup> Armored
        Division. The Army Code Name for the division was Powerhouse. Each unit of the
        division was assigned a Code Name beginning with the letter “P” - CCA Hqs
        Code name was Pyramid. Others were: 66<sup>th</sup> Armored Regiment - Python;
        17<sup>th</sup> Engineers - Pioneer; 41<sup>st</sup> Infantry - Princess;
        142<sup>nd</sup> Signal - Pyrotechnic; 82<sup>nd</sup> Recon -Prowler, etc.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">Placed on alert and on short notice, our
        full strength Combat Command “A” had been detached from the division, along
        with additional support units, and had been sent to augment the light infantry
        fire power of the 101<sup>st</sup> Airborne Division in their desperate fight to
        retain control of the city of Carentan; then under attack by the German <i>6<sup>th</sup>
            Paratroop Regiment and the 17<sup>th</sup> SS Panzer Grenadier Division</i>.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">A week after landing on D-Day, the
        American 101<sup>st</sup> Airborne Division Regiments, including the 501<sup>st</sup>,
        502<sup>nd</sup>, 506<sup>th</sup> and the 327<sup>th</sup> Glider Infantry
        Regiments with depleted numbers from casualties in all ranks, running low on
        ammunition and supplies, were fighting the enemy and exhaustion as their
        adrenaline was spent. In this precarious situation they were in danger of being
        over-run by superior forces, including enemy tanks, which would have resulted in
        the loss of the critical corridor connecting First Army’s V and VII Corps;
        thereby placing Lt. General Omar Bradley’s entire beachhead force in extreme
        jeopardy had this happened.</font><b style="mso-bidi-font-weight:normal">
        <font face="Arial">&nbsp;<o:p>
            </o:p>
        </font>
    </b>
</p>
<p class="MsoNormal" align="center" style="text-align:center;text-indent:.5in;
line-height:200%"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">TIMELY
            ARRIVAL<o:p>
            </o:p>
        </font>
    </b></p>
<p class="MsoNormal">
    <font face="Arial">On June 12<sup>th</sup>, after being
        alerted by Ultra, the secret code breaker, General Omar Bradley learned of the
        German High Command’s plan to launch a counter-attack to divide the V and VII
        Corps boundaries. Bradley immediately directed V Corps Commander, Major General
        Leonard T. Gerow, to order Major General Edward H. Brook’s 2<sup>nd</sup>
        Armored Division’s Combat Command “A” under BG Maurice Rose to move to
        Carentan without delay. The Corps orders were issued for an “Infantry led
        counter-attack, supported by armor.” in hedgerow terrain of the countryside <i>bocage
        </i>totally unsuited for tank warfare. Advancing over the hedgerow was suicidal
        for the infantry and the tanks had to run up the steep bank with their soft
        undercarriage exposed to the anti-tank guns lying in wait. Crossing the trail
        intersections meant risking machine gun fire from the Germans who had zeroed in
        on these keys points, not to mention the snipers.<span style="mso-spacerun: yes">&nbsp;</span>&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">The 327<sup>th</sup> was to attack from
        the north, the 501<sup>st</sup> from the northeast and the 506<sup>th</sup> from
        the southwest, followed by the 502<sup>nd</sup> that would pass through the
        506th. After the scheduled hour for the counter-attack to jump off had passed,
        the weary 101<sup>st</sup> troopers still held fast to their foxholes at the IP
        start line, without attempting to move out as had been ordered. The absence of a
        key Airborne Battalion Commander, who was not at the line of departure, where he
        should have been taking charge, only added to the confusion.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">Unlike their sibling 82<sup>nd</sup>
        Airborne Division, not all of 101<sup>st</sup> Airborne Division soldiers were
        volunteers; many were non-volunteer draftees lacking the famed 82<sup>nd</sup>
        “All American” Airborne Division’s esprite de corps. As such, they were
        not (volunteer) airborne jump-qualified; instead, one regiment was infantry
        glider borne troops who were referred to as “legs” by the airborne jumpers
        who landed in the D-Day drop zone via parachutes. One week after landing, it no
        longer made any difference how you got there, via chute or glider, it was a
        fight for survival and every man counted on the man next to him - with or
        without jump wings.</font><b style="mso-bidi-font-weight:normal">
        <font face="Arial">&nbsp;<o:p>
            </o:p>
        </font>
    </b>
</p>
<p class="MsoNormal" align="center" style="text-align:center;text-indent:.5in;
line-height:150%"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">THE
            GENERAL TAKES COMMAND<o:p>
            </o:p>
        </font>
    </b></p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">The
        CCA Headquarters was already in place, waiting for the General to arrive. We
        didn’t have long to wait as he appeared in his jeep with the red plate on the
        bumper with his one silver star, accompanied by a lone motorcycle driver. Hardly
        much fanfare for the gladiator’s arrival. He appeared to be a tall person as
        he stepped down to be saluted and greeted by his XO (Executive Officer), Lt.
        Col. Benjamin Mart Bailey, Jr. Maps were spread on the canvas covered hood of
        the jeep as his key staff members stood by during the brief discussion. Orders
        were given to “Mount Up” and move out.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">We watched as Brigadier General Maurice
        Rose, growing impatient by the minute, unable to locate a 101<sup>st</sup> field
        grade officer and with the airborne troopers continued failure to move out,
        began walking up the road alone. Ignoring any possible danger, he walked past
        the stunned troopers remaining in their foxholes on the side of the road. They
        looked on with utter amazement as he strode by them as though on parade.
        Compared to all the rest of us dressed in military issue clothing, he stood out
        in his distinctive dove gray Cavalry jodhpurs (trousers), tank jacket and highly
        polished riding boots; a general’s personal dress code was a privilege and
        prerogative. His penchant for Cavalry garb was acquired during his early days
        with the horse Cavalry at Fort Riley, Kansas and the Panama Canal Zone.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">His
        Army issue .45 Colt pistol was worn in his web waist belt holster, with the
        companion eight-inch knife blade sheathe inserted into the leather seam behind
        the flap. They were his inseparable pair of personal side arms in battle, a
        habit formed in North Africa and Sicily. While in his jeep, as the situation
        dictated, he often cradled a Thompson sub-machine gun when on the move with one
        of his task forces, using it on more than one occasion. His Aide seated in the
        rear jump seat was armed in a similar fashion with a Thompson as well; in
        addition to his driver carrying a Thompson in the boot.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">Upon
        seeing a company grade officer from the “Screaming Eagles,” in a clear voice
        heard by all within range, he said in a firm voice, “Captain, let’s get your
        men out of their holes and moving forward. We are going on the attack and I mean … right now!” The firmness
        and tone is all that the order required. Whether
        it was the awe inspiring scenario of a general personally leading an attack or
        the long line of tanks and armored field artillery pieces formed in two columns
        abreast behind him and his staff, it was all that was needed. The troops lining
        the sides of the road started coming out of their foxholes. Hesitantly at first,
        the troops led by their Captain, began to move forward to commence the attack to
        retake that section of the City of Carentan. They had lost it in the last
        skirmish when they were forced to fall back under superior forces and heavy
        enemy fire.</font><b style="mso-bidi-font-weight:normal">
        <font face="Arial">&nbsp;<o:p>
            </o:p>
        </font>
    </b>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center;line-height:
normal"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">DEATH DUEL IN
            THE FIELD</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">At the same moment, unknown to either
        field commander, the German or the American, the German forces began their
        attack at exactly the same hour against what they thought were the thinly
        defended and lightly equipped paratroopers. Now out of their foxholes and out in
        the open, having left their defensive positions, the Germans were vulnerable to
        the firepower unleashed by the heavy tank guns of the 2<sup>nd</sup> Armored 66<sup>th</sup>
        Armored Regiment and the 14<sup>th</sup> Field Artillery howitzer batteries,
        backed by members of the 41<sup>st</sup> Armored Infantry Regiment attached. The
        battle was joined.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">One Task Force attacked west along the
        Carentan-Baupte Road at 1400 and followed by the 502<sup>nd</sup> Parachute
        Infantry, passed through the 506<sup>th</sup> Parachute Infantry, driving
        westward. A second Task Force attacked along the Carentan-Periers highway. Both
        columns receiving close support from the 14<sup>th</sup> Field Artillery
        Battalion.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">The
        Germans were totally caught off guard by surprise. Despite their determined
        assaults and with armored support, they were repulsed each time, suffering a
        high number of casualties, including 500 men killed and over 1,000 wounded. CCA
        suffered eight men KIA, including two officers. Conversely, only six of the
        enemy were taken prisoner, indicating the fanatic fierceness of the close combat
        fighting that ensued.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">While in the city of Carentan, we came
        under some heavy shelling so I took cover in the first convenient place I could
        find, a bookstore. A soldier from the 101st Airborne had the same idea at the
        same time and we collided in the doorway. He turned out to be a friend of mine I
        had met on the Liberty ship that had brought us overseas together … Jim
        Magruder. In one on my letters I wrote home to my folks, I wrote that Magruder
        and I had met in “Some where in France”.<span style="mso-spacerun: yes">&nbsp;
        </span>By mentioning that he was in the airborne, I was able to slip that by the
        censors and by doing so, they would know of my location in Normandy, as the
        presence of our division in combat had not yet been released for publication.</font>
</p>
<p class="MsoNormal" align="center" style="text-align:center;line-height:200%"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">THE
            BAND OF BROTHERS</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">Carentan was saved and the danger of
        splitting the two Corps areas was passed. Fifty years later, men of 506’s Easy
        Company in Stephen Ambrose’s book “<b><i>Band of Brothers</i></b>” would
        express their gratitude for Combat Command “A” of the 2<sup>nd</sup> Armored
        arriving in a timely fashion, without whom their fate might well of have had a
        far different outcome.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal" style="line-height:150%">
    <font face="Arial"><span style="mso-tab-count:1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        </span><b><i><span style="font-size:10.0pt;mso-bidi-font-size:12.0pt">Whoever
                    does not have the stomach for this fight, let him depart. Give him money to
                    speed his departure since we wish not to die in that man’s Company. Whoever
                    lives past today and comes home safely will rouse himself every year and on this
                    day, show his neighbors his scars, and tell embellished stories of all their
                    great feats of battle. These stories will teach his son and from this day until
                    the end of the world, we shall be remembered. We few, we happy few, we band of
                    brothers; for whoever shed his blood with me shall be my brother. And those men
                    afraid to go will think themselves lesser men as they hear of how we fought and
                    died together. ---</span></i></b><i><span style="font-size:8.0pt;mso-bidi-font-size:
12.0pt"> William Shakespeare wrote in Henry V, the words of the King on the day
                of the great battle.</span></i></font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">We
        did not learn until long after the war was over just how close we came to losing
        the Omaha Beachhead, had the Panzers been able to drive the wedge between the
        thinly defended V and VII Corps boundaries at Carentan and the Vire River. If
        Hitler had released the full armor reserves in France to Rommel, the Allies
        would have been over whelmed and thrown back into the sea.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">As the only American armored division
        then on the Beachhead, the 2<sup>nd</sup> Armored alone would not have had
        sufficient firepower to stop them. We had nothing in our arsenal to compete with
        heavier German King Tiger’s sloped frontal armor plate, equipped with the
        famed dual purpose 88, nor the Mark IV and Mark V with the 75mm cannon --
        against our thin-shelled light Stuarts (37mm) and Sherman (74mm) tanks with
        inferior weapons. We were out-numbered and out-gunned.</font><b style="mso-bidi-font-weight:normal">
        <font face="Arial">&nbsp;<o:p>
            </o:p>
        </font>
    </b>
</p>
<p class="MsoNormal" align="center" style="text-align:center;text-indent:.5in"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">THE
            GENERAL KEEPS HIS WORD</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">During
        the early days in the Carentan area as Veno, Hull and I, were repairing a line
        from the Command Post, we saw a jeep rapidly approaching with the red metal
        plate attached to the front bumper with two silver stars – meaning a two star
        general. We stopped repairing and testing the splice in the line, stood at
        attention and gave him the required hand salute. Standing upright in the jeep,
        holding onto the grab bar attached to the jeep dashboard, (Standard Operating
        Procedure for our 2AD generals) he returned our salute. His driver stopped the
        jeep and backed up to where we standing - it was our Division Commander, Major
        General Edward H. Brooks.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">Obviously
        he knew we were wiremen, but he asked what unit we were in and when was the last
        time we had a hot meal. We said CCA Hqs and that we had been eating K and C
        rations for over a week. He mentioned the critical importance of his wire
        communications - to that extent, his Aide carried a EE8A Field Telephone with
        needle-nose alligator clips attached to a short lead that he could use to clip
        on to any telephone line to connect to his headquarters for an up to the minute
        situation report. With that he said he would send someone up with a hot meal.
        Another salute and he was gone. The pat-on-the-back from the CG was nice, but
        the promise of a hot meal was better. In less than an hour, a Mess Sergeant from
        some outfit brought us two marmite containers with breaded pork steaks, among
        others things. The Sergeant wasn’t hanging around so we ate what we could,
        gave him back his cans and stuffed the leftover pork steaks in the glove
        compartment to eat later. We forgot about them and all too late we had to throw
        them away.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">After
        being relieved, CCA was returned to the division assembly area to prepare for
        movement to an area Northeast of Caumont to take a position on the line to
        relieve the British Seventh Armoured Division, known as the “Desert Rats”
        from their North Africa combat experiences. We remained here until 17 July 1944
        when we were relieved by the British. The Combat Command<span style="mso-spacerun:
yes">&nbsp; </span>“A” then assembled in the area of La Catherine, France,
        to await movement orders for the St. Lo Breakout.</font>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center">
    <font face="Arial">PART
        II</font>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">THE
            MAN BEHIND THE MASK</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">Another
        incident regarding General Rose’s character that I recall also happened in
        Normandy in early July 1944. We had taken over defensive holding positions on
        the Caumont Line, south of Livry, and placed in a static situation; punctuated
        only by artillery duels. Infantry patrols were sent out nightly to capture
        prisoners for information.<span style="mso-spacerun: yes">&nbsp; </span>In the
        meantime, the beachhead continued to rapidly expand, unloading troops and
        supplies behind us in preparation for the expected St. Lo Offensive and
        Breakout.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">Our
        Headquarters Command Post was dug in behind the cursed and damned hedgerows with
        camouflage nets hung over all vehicles and equipment; hopefully to escape
        detection from our nightly German visitor from the air, named Bed Check Charlie,
        photographing our “hidden” locations. This in turn would result in frequent
        and heavy artillery barrages that would rain down on us the next day.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">Lt. Colonel Jesse M. Hawkins, Division
        G-2, ordered the Wire Section of the 142<sup>nd</sup> Signal Company to install
        a public address system located at an advanced point in the woods, <i>half way</i>
        between our front line and the enemy positions. Our 142<sup>nd</sup> Signal Wire
        Section Chief, Technical Sergeant Thomas McFarland, was awarded the Silver Star
        (2AD G.O. 30, dated 23 May 1945) for bravery by volunteering to make the wire
        installation of the P.A. system. Messages were then broadcast in several
        languages, encouraging defections by the Polish, Russian and German troops
        facing us. The first night it had a modicum of success with three deserters who
        came in, willing to speak over the public address system to their fellow
        Wehrmacht soldiers to validate the American promises of their safe passage of
        surrender.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">On
        this day both of our wire vehicles received damage from shell fragments from
        tree-burst shellfire that landed in the crab apple orchard, although we had
        painstakingly covered the jeep and truck with camouflage nets to avoid
        detection. My field-pack, out in the open, was destroyed when ripped with shell
        fragments. Fortunately for me, it was not on my back at the time.</font>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center;line-height:
200%"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">BRAVERY &amp;
            COWARDICE</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">While
        on the receiving end of an artillery barrage, when suddenly the cry “medic”
        was heard from one of our men who was wounded.<span style="mso-spacerun: yes">&nbsp;
        </span>Disregarding the continuous in-coming harassment fire we were taking, XO
        Lt. Col. Bailey, Jr., searching for our Medical Officer, Captain John Ryan,
        M.D., found him hunkered-down in his foxhole. After an exchange of heated words,
        Col. Bailey gave him a direct order (ultimatum), which Ryan “reluctantly”
        obeyed, to immediately go to the man’s aid. Our 27-year-old West Point Colonel
        (Class of 1939) earned a lot of our respect at that moment and in the days and
        nights that followed. Times like this motivated us to dig our hole deeper as
        soon as the incoming shellfire let up. It was not an uncommon early morning
        occurrence to see the dirt flying out of holes presumed to having been thought
        adequate the night before.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">To compound matters, Lt. Col. Carl I.
        Hutton’s 14<sup>th</sup> Field Artillery Battalion 105 mm Howitzer Batteries
        were located immediately to our rear, less than half a mile, firing
        counter-battery fire around the clock. In return, the 14th (and us) drew
        counter-battery answering fire and we received the short rounds from the German
        88s in retaliation, also around the clock. The well-positioned enemy forward
        observers called for random firing at all hours. Movement was kept to a minimum
        by day and only at night outside of our foxholes by necessity. Answering the
        call of nature was done quickly and with some personal peril.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">The
        frequent rains that fell intermittently daily didn’t help matters much in our
        cramped and often-muddy foxholes we dug in the ground at the base of the 10-foot
        high hedgerows. We had the choice of attempting to cover the entrance to our
        hole in the ground with our raincoats or wearing them to try to keep our smelly
        gas impregnated clothing from becoming more caked with mud - neither option
        worked well. Even the damn mud was unfriendly.<span style="mso-spacerun:
yes">&nbsp; </span>We had given up on the porous tent shelter-half as a cover
        during the first few nights of rain. My foxhole buddy, Larry Hull and I would
        continually chop away at the decades old tangled roots from the hedges above us,
        enabling us to dig deeper into the damp soil beneath - after one week it soon
        appeared to have the beginnings of a tunnel!</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">In
        spite of the present danger of sporadic incoming shellfire, General Rose had a
        small olive drab field tent erected behind one of the earthen hedgerow banks in
        the command post area nearby. He would be seen seated on his folding canvas
        chair, using his EE8A field phone, studying maps on the table, issuing orders;
        disregarding and seemingly oblivious to the danger of the incoming rounds.</font><b
        style="mso-bidi-font-weight:normal">
        <font face="Arial">&nbsp;<o:p>
            </o:p>
        </font>
    </b>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center;line-height:
200%"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">MILITARY
            DISCIPLINE<o:p>
            </o:p>
        </font>
    </b></p>
<p class="MsoNormal">
    <font face="Arial">On another occasion, I witnessed two of
        his top tank commanders, Colonel John “Peewee” Howell Collier, 66th Armored
        Regiment CO and LTC Lindsay Herkness, 66<sup>th</sup> 2<sup>nd</sup> Bn CO,
        standing at <i>attention</i> directly in front of the tent as he sat facing
        them, tapping the side of his boot with his riding crop (another old habit from
        his Cavalry days). He was briefing them in detail on their respective
        assignments and what he expected from their tank units. I was amazed by the
        rigid code of formalities while periodically incoming shellfire was landing
        close by in the fields in front and behind us. At this time, I could only think
        of the old cliché, “Discretion is a more important value than valor.”-- but
        not the General, regardless of what the two colonels and I might have been
        thinking.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">Strict
        military courtesy was constantly observed in his presence and that included all
        of his field grade officers, including his Executive Officer Lt. Colonel
        Benjamin Mart Bailey, Jr. (KIA 23 AUG 44 near St. Andre, France). Needless to
        say, the same standards held true for all enlisted men, of course. The
        motivational factor was respect and fear! His icy stare from those cold dark
        eyes was more than enough to make a junior grade officer duck for cover. He was
        never known to rebuke a subordinate in the presence of others, but capable of
        rendering a biting tongue lashing in private when circumstances warranted. A
        strict disciplinarian, he conducted himself in a manner that earned the respect
        of his staff - with one notable exception. A junior grade staff officer,
        Captain Shadrack Turner, said privately to Lieutenants Louis Bifano and Philip
        B. Reisler, “That son-of-a-bitch (Rose) is going to get us all killed!”
        Captain Turner requested to be relieved from his staff position and the request
        granted at Tidworth Barracks, England.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">His battalion and task force commanders
        knew from past experiences that he would never give them an order that he would
        hesitate to carry out himself. That is not to say that he did not push men to
        their limits, and sometimes beyond, to achieve <i>his </i>success on the
        battlefield, regardless of the calculated costs of men and equipment.
        Nevertheless, when given an assignment and objective by a superior, he expected
        casualties and issued the orders that knowingly sent men to face death without
        hesitancy on his part.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">At
        times, he could be rightfully accused of micromanaging by constantly prodding
        the task force commanders to “Keep pushing. Keep moving.” as he monitored
        their radio nets and conversations. Not only did he insist upon maintaining
        radio contact with them, but also he always positioned himself, along with his
        staff at or near the point of attack as he constantly tempted fate by riding the
        razor’s edge to command at the front line.</font>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center">
    <font face="Arial">PART
        III</font>
</p>
<p class="MsoBodyTextIndent" align="center" style="text-align:center"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">THE
            PROMOTION</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">After
        the Breakout at St. Lo, BG Maurice Rose’s demonstrated sang-froid demeanor as
        a hard driving relentless taskmaster were recognized by both the VII Corps
        Commander, Lt. General J. Lawton Collins and First Army CO, General Omar
        Bradley, as the leader needed to take command of the faltering Third Armored
        Division; then under the command of Major General Leroy Watson; who was
        relieved, reduced in grade to Colonel and reassigned, to be replaced by
        Brigadier General Maurice Rose on 7 August 1944.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">Maurice Rose, a man without many
        intimate and close friends, after more than two decades spent climbing the
        solitary ladder of success in military service, departed the 2<sup>nd</sup>
        Armored Division exactly in the same manner he arrived - <i>alone</i>. When he
        received the promotional transfer he failed to keep with tradition and did not
        request joint transfer orders to retain his Aide de Camp, his personal Orderly
        nor his jeep driver to accompany him -- all who had served him faithfully and
        shared his dangers. They were left behind, returned to the personnel pool and
        quickly forgotten. The loner was moving on.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">It
        was the Old Soldier-s unwritten creed that when the Army cuts your PCS
        (Permanent Change of Station) transfer orders, all old debts are cancelled, IOUs
        forgotten and once close friendships end. Maurice Rose had acquired this custom
        early in his career as a soldier. He steadfastly focused solely on the present
        and his future, obviously not concerned with the past or the obligations of
        conscience. The philosophy being when one door in life closes, another opens.
        This self-serving role of an egocentric would occur in his private life as well.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoBodyTextIndent" style="text-indent:0in;line-height:normal">
    <font face="Arial">Was
        this questionable psyche that of a man driven by un-bridled ambition to rise to
        the top or simply one more of the numerous heretofore-unknown quirks and flaws
        in his character?<span style="mso-spacerun: yes">&nbsp; </span>At the young age
        of 19, while recovering in an Army hospital he claimed his religious preference
        was Protestant, so did he truly <i style="mso-bidi-font-style:normal">convert</i>
        to Christianity, as falsely rumored after his death? There is no record of his
        baptism or confirmation to the Christian faith. Or had he become an apostate and
        renounced his faith as a Jew? Others suspect he remained a closet Jew who”went along to get along”? No one
        knew him intimately and his reputation as an
        enigma grew with the myths surrounding his life. His personal religious
        preferences/beliefs and two marriages, the first to a Mormon and the second to
        an Episcopalian, remained a closed subject with which no one had knowledge until
        fifty odd years after his death.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">This saga of the man, who enlisted as an
        Infantry Private at age 17 in World War One, was wounded in action, received a
        commission as a Second Lieutenant and rose through the ranks to where he was
        promoted to Major General and placed in command of one of the three existing
        full heavy (1<sup>st</sup>, 2<sup>nd</sup> and 3<sup>rd</sup>) armored divisions
        of 14,500 men whose destiny he controlled, was about to begin.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">History would now commence to record his
        specific military achievements beginning with the Battle For Mons, Belgium, with
        his soon to become famous “Spearhead” 3<sup>rd</sup> Armored Division, the
        tip of the spear of the VII Corps, in the remaining months of the war. He had
        finally reached the pinnacle he had sought all his life - his own division.<span
            style="mso-spacerun: yes">&nbsp;
        </span>He came well prepared for the challenge to mold the command to his
        strident demands in very short order. The early years spent under Generals
        Patton, Harmon and Brooks served as his informal textbooks making him the ideal
        staff officer. Generals Collins and Bradley could not have made a wiser choice.
        They could not have chosen a more qualified and dedicated warrior needed for the
        task. The “Spearhead” Division had their decision-making problems at the
        command level and not down at battalion and company levels. This time the Army
        had for once put the square peg in the square hole.</font><b style="mso-bidi-font-weight:normal">
        <font face="Arial">&nbsp;<o:p>
            </o:p>
        </font>
    </b>
</p>
<p class="MsoNormal" align="center" style="text-align:center;text-indent:.5in;
line-height:200%"><b style="mso-bidi-font-weight:normal">
        <font face="Arial">THE
            MUSTANG EARNS HIS SPURS</font>
    </b>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">While lacking a commission from West
        Point or one of the other accredited military academies, and without a basic
        college degree; this promotion to a combat division commander was heralded by
        his troops as their “<b><i>Mustang Officer</i></b>” who rose from a rear
        rank buck Private to the top. That he had paid his dues is an understatement.<span
            style="mso-spacerun: yes">&nbsp;
        </span>His tactical reputation and respect would become legendary with his men
        of the 3<sup>rd</sup> Armored Division as well as others of the famed VII Corps
        and the First Army.</font>
    <font face="Arial">&nbsp;<o:p>
        </o:p>
    </font>
</p>
<p class="MsoNormal">
    <font face="Arial">Less than eight months later in the
        spring of 1945, just before the close of hostilities, while leading this
        decorated unit in the last campaign, he would lose his life on the battle field;
        leaving behind to mourn his loss, his elderly parents and two women, each with a
        son - both women having named their only child after the father, Maurice Rose.
        That is the abbreviated life story of Maurice Rose, the son of a Rabbi, who was
        a Jew by birth and Christian by choice, <b><i>The Mustang General</i></b> -- The
        Man From Colorado.</font><i><span style="font-size:10.0pt;mso-bidi-font-size:12.0pt">
            <font face="Arial">&nbsp;<o:p>
                </o:p>
            </font>
        </span></i>
</p>
