<h1 align="center">
    <font face="Arial">THE BIG HANDS</font>
</h1>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; On 5 March 1945, Combat Command &quot;A&quot; had passed
        between Krefeld and Uerdingen, Germany, having already taken Munchen-Gladbach.
        Our command had overran and destroyed thirty-six 88mm dual purpose guns in an
        area about 2,000 square yards after having overcome a fanatical defense in the
        built-up areas southeast of the city of Krefeld, where the German troops had
        employed numerous bazookas and also panzerfausts in very close and desperate
        hand to hand fighting. Very few prisoners surrendered, leaving many wounded
        behind as they withdrew.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; CCA Headquarters then assembled in the vicinity of
        Korschebroich, where we waited for orders to cross the mighty Rhine River on the
        night of 27-28 March, 1945, just south of the town of Wesel.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; It was here at Korschebroich while waiting that our GIs
        found the underground stored vats of wine. Most everyone dumped their 5 gallon
        water cans and refilled the Jerry cans with the looted Rhine valley wine.
        Perhaps the liberated bug juice is what caused the guy with the Big Hands to
        settle our own private war right here. This same time is when our very own
        Indian from Lake Okeechobee, Florida, Private Walter Hogan, consumed far too
        much of the potent grape juice and became inebriated and out of control. While
        in this state of mental impairment, he unwisely decided to wake up our
        commander, Brigadier General John H. (PeeWee) Collier, for an old fashioned GI
        &quot;piss call&quot; in the middle of the night. That and the outcome are
        another worthwhile story and have been told elsewhere. (See 2AD Bulletin #3,
        1985)</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; The day it happened I was cold sober and seated at the
        kitchen table in one of the German apartment style homes that we had taken over
        as our quarters. I had been writing a letter home to my parents, as was my
        frequent custom whenever time permitted. I had the room to myself with a good
        hot fire in the kitchen coal heated cook stove as I sat collecting my thoughts
        of what I could get past the censors as it was always a challenge to match wits
        with the brass. The rest of the crew was spread throughout the house playing
        cards or loafing, when in walked Private Nelson Fountain from Wyandotte,
        Michigan. He was one of our new division replacements conscripted from the
        non-descript rear echelon to fill one of the many vacancies that all of our
        units had suffered after the Battle of the Bulge. At the time, he was in his
        late thirties, and had not spent any previous time in combat, compared to those
        of us in our 20s who had been since the D-Day landings, making us the veterans
        and him the rookie.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Fountain had the biggest pair of hands I had ever seen. I
        wore a size nine and in comparison he must have worn an extra large size 12 or
        13. I mean he had big hands! Besides having oversized hands he had a big mouth
        to match those baseball glove sized hands of his. He also had an irksome habit
        of talking with his hands, so that you could not avoid noticing the big mitts.
        Quite possibly this was done to impress and intimidate people. Even sober he was
        argumentative and obnoxious in discussing any topic to the point of being
        belligerent when told to do something. In addition, because Fountain was near 40
        years of age, he had this habit of calling me &quot;kid&quot; - knowing I was
        but 22 years old. None of which I appreciated, especially coming from a
        newcomer, but I kept my peace and let Veno run the crew - until that day when
        push came to shove. It had been brewing as my resentment to his snide remarks
        and general attitude began to fester with each passing day. Now it was just a
        matter of time until he lit my known short tempered fuse. The others saw it
        coming.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Shortly after this incident, Fountain walked into the
        kitchen and I could readily see he had been sucking up too much wine before
        lunch and was spoiling for trouble. He had come to the right place to find it.
        The time had come to settle up. The situation had been brewing for a few
        previous days as his wise-ass remarks became a daily exercise of his blow-hard
        summations. The abundance of wine must have been what was needed to confront me
        although he wasn't too drunk not to know what he was saying or doing.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; He came up to the table and started riding my ass
        regarding my always writing letters home instead of drinking with him and the
        guys. I told him to back off and mind his own damned business, but he wanted to
        push it to the edge and continued. I had had enough of him and his mouth and
        felt here we go.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; My last trip to fist city was almost two years to the
        date, when my old First Sergeant Conway invited me &quot;out on to the
        green&quot; back behind the Regimental Theatre after sundown at Camp Crowder,
        Missouri. Although I was out-weighed by 20 pounds, I gave as &quot;good&quot; as
        I got, as the expression goes - and gained his respect!</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; This time I intended to do much better for myself, because
        this time the first punch to be landed would be mine. One more time I told him
        to back off, but he responded, &quot;Marsh, what are you gonna do if I reached
        over and tore up that sheet of paper you're writing on?&quot; I told him,
        &quot;Try it and let's find out.&quot; Hovering over me and without taking his
        bloodshot eyes from my face, his right hand reached out and grabbed my letter
        from the table.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; In his raspy voice, he started to say, &quot;Well, kid
        there â€¦.&quot; And I never let him finish the sentence as I grabbed his right
        hand with my left and smashed him in the mouth with my right as I rose from the
        table all in one motion before he was able to ball up his other fist. I nailed
        him a solid punch because I could feel the needles shoot up my arm to my elbow
        on impact as bone struck bone. I felt it was the hardest punch I've ever landed
        in a fist fight as it caught him flush on the jaw. Now I turned loose and
        continued punching him in the face and body with both hands as fast as I could
        throw punches, rocking him back on his heels, forcing him back onto the hot
        stove. Hearing all the racket and curses, Veno, Hull and Donahue came rushing
        into the room to separate us and pulled us apart. Looking at his bloody mouth I
        learned that with my first punch that I had split his lips and cracked his lower
        denture plate. Until then we did not know he wore a full set of dentures - upper
        and lower.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; From that moment on, his attitude and respect towards me
        changed and we never spoke to each other unless absolutely necessary, if at all.
        And then never in a friendly vein. Veno got rid of him shortly after we crossed
        the Rhine River as the war wound down and we came off the line forever on April
        22nd - our war had ended. We assembled in Wolfenbuttel and I last saw him there
        in May when the war officially ended.</font>
</p>
<p>
    <font face="Arial">&nbsp;&nbsp;&nbsp; Someone from the Company asked, &quot;Did you notice the
        size of that guy's hands?&quot; I answered, &quot;Sure did. Big hands, big mouth
        - mano, mano, makes no difference and sure as hell doesn't make him the better
        man.&quot;</font>
</p>
