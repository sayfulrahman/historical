            <font size="3" face="Arial">By Don R. Marsh</font>
        </em></p>
    <p style="MARGIN: 0in 0in 0pt">
        <font face="Arial">December 1943</font>
    </p>
    <p style="MARGIN: 0in 0in 0pt">
        <font face="Arial">
            <st1:place w:st="on">
                <st1:City w:st="on">
                    Cucklington</st1:City>
                , <st1:country-region w:st="on">
                    England</st1:country-region>
