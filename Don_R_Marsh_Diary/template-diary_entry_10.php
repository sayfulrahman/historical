<h1 align="center">
    <font face="Arial">THE ARDENNES CAMPAIGN&nbsp;</font>
</h1>
<p align="center"><b>
        <font face="Arial">By Don R. Marsh</font>
    </b></p>
<p>
    <font face="Arial">23 December 1944&nbsp;</font>
</p>
<p>
    <font face="Arial">Ciney, Belgium</font>
</p>
<p>
    <font face="Arial">After having been relieved by the 29th Infantry Division, we pulled out of
        Baesweiler, Germany as a heavy rain began falling. With three hours notice, we
        mounted up and began the 75-mile night road march to an assembly area at Les
        Avins near Huy, Belgium. We took our place in the single-line convoy stretched
        out for miles. Driving the converted personnel carrier without a top or
        windshield seated in one of the two canvas covered front bucket seats in the
        driving freezing rain was a nightmare to remember.</font>
</p>
<p>
    <font face="Arial">In less than 22 hours, all the Division combat vehicles had made the 75-mile
        trip and began assembling in defensive positions while sending out patrols. The
        first contact with the enemy came on 23 December, blocking the road to Namur
        near Haid, from the advance of the German 2nd Panzer Division leading the
        enemy's deepest penetration after their seven day 60-mile devastating rampage
        advance through the American lines.</font>
</p>
<p>
    <font face="Arial">We had been officially transferred from the US 9th Army back to the 1st Army,
        VII Corps once again; however, the American First Army was placed under British
        General Bernard Montgomery's Command of the northern shoulder. Monty's orders
        were to withdraw farther west on the 24th to form a defense line and &quot;tidy
        up the front&quot; without taking any action. Our 2nd Armored Division CO, Major
        General Ernest &quot;Old Gravel Voice&quot; Harmon disregarded that order and
        moved to block the advance near the village of Ciney. The Recon scouts sent word
        that the Germans had stopped near Celles, apparently to allocate the fuel now in
        short supply.</font>
</p>
<p>
    <font face="Arial">DEATH OF A DIVISION</font>
</p>
<p>
    <font face="Arial">At 1430 hours Harmon called General Lawton J. Collins VII Corps Headquarters
        and said,&quot;One of my patrols just spotted the Krauts coiled near Celles.
        Belgians say the Krauts are out of gas. They're sitting ducks. Let me take the
        bastards!&quot;</font>
</p>
<p>
    <font face="Arial">At 1435 hours Harmon told VII Corps, &quot;We've got the whole damned 2nd
        Panzer Division in a sack! You've got to give me immediate authority to
        attack!&quot; Despite Collins grave misgivings of disobeying Monty's orders, he
        gave Harmon the OK.</font>
</p>
<p>
    <font face="Arial">At 1625 hours Harmon told VII Corps, &quot;The bastards are in the bag!&quot;
        On this day the German 2nd Panzer Division trapped and unable to maneuver was
        destroyed. The enemy lost 81 tanks, 7 assault guns, 405 vehicles of all types,
        plus 74 big guns. An actual account of the enemy killed and captured was not
        recorded. It ceased as a fighting force. The German 9th Panzer Division
        desperately attempted to rescue the 2nd Panzer, but was beaten back with severe
        losses.</font>
</p>
<p>
    <font face="Arial">On 23 December 1944, CC&quot;A&quot; attacked south and east from Ciney,
        Belgium despite very heavy resistance as the attack progressed on the enemy
        flanks at Leignon, Buissonville, Havrenne, Humaine and Rochefort to a general
        line north of Rochefort. Very heavy fighting took place at Humaine where the
        enemy put up a fanatical defense with armor and infantry. The Command met
        numerous counter-defenses by combined arms.</font>
</p>
<p>
    <font face="Arial">In a thickly wooded area a number of the enemy had frozen to death right
        where they fought and died of wounds. An enemy machine gunner and assistant
        froze to the ground; both prone and propped on their elbows still holding onto
        their MG with the assistant alongside of him feeding the belt to the MG. Other
        than checking the bodies for souvenir side arms (Lugers or Walther P-38
        pistols), they drew hardly a glance as we passed through the forest trail. While
        the column paused in the advance, it was here in the dark dense wooded area that
        Major General Ernest Harmon, the 2AD CO, drove up in his M-8 Greyhound Recon car
        to confer with Brigadier General John H. Collier, Combat Command A Commander,
        seated in his jeep. As I watched the meeting, I noticed BG Collier wearing a
        pair of Air Corps bomber crew's brown leather sheepskin lined boots and the
        sheepskin lined collar brown leather jacket. Must be nice to have friends in
        high places! General Harmon needled Collier by asking,&quot;What the fuck is the
        hold up?&quot; The reporters didn't call General Harmon &quot;Profane
        Ernie&quot; without reason. Harmon was prone to frequently use the GI common
        &quot;F&quot; word, among others.</font>
</p>
<p>
    <font face="Arial">NO PEACE ON EARTH</font>
</p>
<p>
    <font face="Arial">24 December 1944, Christmas Eve, absolutely no Christmas spirit and no
        caroling tonight, just the opposite - no peace on earth. Death is all around us.
        We were on the outskirts of Humain and Buissionville fighting in a heavily
        wooded area with waist deep snow and frigid temperatures near or below zero.
        Nobody was talking, but to a man we felt the twinge of homesickness and
        nostalgia. Who ever had started that damned rumor in the fall that we would be
        home by Christmas was severely mistaken and constipated. We got through the
        night and the next day wasn't much better.</font>
</p>
<p>
    <font face="Arial">Nearby at Hotton, Belgium, my friends in the 143rd Signal Company were
        fighting to hold a key bridge under assault by superior strength enemy tank
        forces. My close friend Kenny Speers would be among those wounded Christmas Eve
        while defending the bridge at the cross-roads village; along with my friend 2nd
        Lt. Jack B. Warden and his under strength platoon of men from B Company 36th
        Infantry. At Havrenne, enemy tanks and infantry attempted to infiltrate during
        the hours of darkness and succeeded to getting within 50-yards of our positions
        before being destroyed in a desperate but brief action.</font>
</p>
<p>
    <font face="Arial">Due to radio silence imposed by the Division, communications were very poor
        or non-existent other than by jeep driven messengers or our command wire; both
        were placed under extreme peril due to the fluid lines. A road once thought to
        be open would be cut and under enemy control of recon patrols and larger forces.</font>
</p>
<p>
    <font face="Arial">On 28 December 1944, we were relieved by British troops, who (in accordance
        with General Montgomery's directives) had assembled far to the rear near Namur.
        Our Command moved east over icy, snow packed roads to a new area in the vicinity
        of Forzee-Mont Gauthier, Belgium, in preparation for the attack on Houffalize.</font>
</p>
<p>
    <font face="Arial">SEALING THE BULGE</font>
</p>
<p>
    <font face="Arial">First Army's plan under General Bradley was to drive south and southeast to
        link up with General Patton's Third Army driving up from the south. The key
        junction city was Houffalize, Belgium. Bradley chose General Collins tough VII
        Corps comprised of the two heavyweight armored division, the 2nd and the 3rd
        under the best two armored commanders, Major Generals Ernest Harmon and Maurice
        Rose. Attacking abreast, with the 84th Infantry flanking the 2nd AD and the 83rd
        Infantry flanking the 3rd AD the attack began. Our division struck south from
        the Hotton-Grandmenil road, operating on a nine mile front. One village after
        another fell to our combined attack - among them, road centers Dochamps and
        Samree, and other key villages of Beffe, Devanave, Wibrin, Achouffe, Trinal,
        Magoster, Odeigne and Freyneux. The 66th Armored Regiment's Commander, Colonel
        C. J. Mansfield was killed in action in the battle for Samree.</font>
</p>
<p>
    <font face="Arial">We were in the column with the Headquarters 3rd Battalion of the 66th Armored
        Regiment under the command of Lt. Col. Hugh H. O'Farrell where I watched from a
        high ground vantage point as our tanks knocked out the German strong points.</font>
</p>
<p>
    <font face="Arial">Despite the employment of terrific concentrations of artillery, the enemy had
        to be killed or forced to withdraw by tanks and infantry which attacked under
        the cover of darkness or in blinding snow flurries to close with the enemy in
        their dug-in positions. On 16 January, as leading elements of the 41st Infantry
        Regiment of our CC&quot;A&quot; overlooked Houffalize from high ground
        immediately northwest, contact was established at 0640B with elements of the
        11th Armored Division of Patton's Third Army. The Battle of the Bulge was now
        officially over. The American Army had suffered tremendous losses of personnel
        killed, wounded and thousands who surrendered and were captured, plus the
        staggering loss of equipment. In the end, it was a hollow victory, resulting in
        a bitter lesson caused by the risks and calculated gamble made by Generals
        Eisenhower and Omar Bradley in sanctioning the weak and thin line of defense
        that winter, inviting disaster.</font>
</p>
