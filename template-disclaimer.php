            troop had been were we were heading. Over the next hill always on the alert for
            a sign that we were going to be attacked by an enemy force, artillery, mortar
            fire or a German sniper, or head on into a German tank. From June 1944 until the
            war was over in early May 1945 we were the enemy. The German forces were out to
            get rid of us any way they could, we could be hit with deadly force at anytime
            during this period of time. This is what we call stress. It is a miracle to be
