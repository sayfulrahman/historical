    <h3 align="center">
        <font face="Verdana"><b>Civilian Conservation Corps</b>
            ( <b>CCC )</b></font>
    </h3>
    <p align="center"><b>
            <font face="Verdana">They sacrificed for their family, and their country, they
                were able to stay the course, in the hard times and then in the good
                times.&nbsp;&nbsp;&nbsp;</font>
        </b></p>
    <p>
        <font face="Verdana">In 1933, I was nine years old, my father owned a general store. Times were
            hard, no work, no money.&nbsp; During this time in1933 to 1942,&nbsp; we would see young men
            ( 18 to 25 ) go by in open trucks. They were what we called <b>CCC</b> men,
            which was&nbsp; &quot; Civilian Conservation Corps&quot; , it was a newly elected presidents
            idea, President <b><u>Roosevelt</u></b>. They came from economically depressed families from
            all over the country . They received $30,00 per month, out of this they had to
            send an allotment home of $25.00, they could keep $5.00 for themselves, they run bills at the
            store also, but they paid them all. Our school team played ball with them, some
            of the 3-CCC men dated local girls, later would marry them. .Camps controlled by
            the army, transported by the army between camps.&nbsp; We did not know
            that we may go to war, this was valuable training for the army in the mass
            movement of troops during <b> WW 2.</b> The CCC helped in flooded disasters and forest
            fires throughout the United States.&nbsp; When released from the CCC,
            most of them enlisted in the military service, They brought with them
            discipline, and army procedure, marching and etc. Over 5 million ended up in
            military service, distinguishing themselves in every branch of service. They
            were involved in the very important job of the early training of recruits for WW
            2. The CCC would start in 1933 and disband in 1942.</font>
    </p>
